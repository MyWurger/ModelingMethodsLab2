// Назначение файла:
// 1) объявить набор вспомогательных функций для подготовки и обслуживания
//    графиков в PlotChartWidget;
// 2) вынести из GUI-классов численные и геометрические расчеты,
//    связанные с делениями осей, интерполяцией и прореживанием точек;
// 3) централизовать константы, которые определяют поведение zoom,
//    стрелок осей и ограничений на число отображаемых точек.
//
// По архитектурной роли это чистый utility-layer для графиков:
// - без прямой привязки к виджетам интерфейса;
// - с логикой, которую удобно тестировать и сопровождать отдельно;
// - с функциями, которые помогают PlotChartWidget и InteractiveChartView
//   оставаться менее перегруженными.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем PlotSeriesData и связанные графические типы,
// потому что часть функций работает с режимами hover конкретных серий.
#include "PlotChartWidget.h"

// QPointF нужен для представления точек графика в вещественных координатах.
#include <QPointF>
// QString используется для форматирования значений hover-подсказок.
#include <QString>
// QVector используется для хранения массивов точек графика.
#include <QVector>

// std::optional нужен там, где функция может не найти корректного значения
// на запрошенной координате X.
#include <optional>

// ----------------------------------------------------------------------------
// ПРОСТРАНСТВО ИМЕН PlotChartUtils
// ----------------------------------------------------------------------------
// Назначение:
// Сгруппировать все служебные функции и константы,
// относящиеся к численной стороне графиков.
// ----------------------------------------------------------------------------
namespace PlotChartUtils
{
// Максимальное число точек, которое допускается реально рисовать на графике
// после прореживания. Это ограничение защищает интерфейс от деградации
// производительности на очень больших выборках.
constexpr int kMaxRenderedPoints = 20000;

// Минимальный коэффициент zoom за один шаг.
// Меньше этого значения масштабирование за одно действие делать нельзя,
// чтобы интерфейс не "прыгал" слишком резко.
constexpr double kMinZoomFactor = 0.75;

// Максимальный коэффициент zoom за один шаг.
// Это симметричное ограничение для слишком агрессивного увеличения.
constexpr double kMaxZoomFactor = 1.35;

// Базовый множитель для zoom колесом мыши.
constexpr double kWheelZoomStep = 1.15;

// Длина наконечника стрелки внутренних осей.
constexpr double kAxisArrowLength = 9.0;

// Половина ширины наконечника стрелки внутренних осей.
constexpr double kAxisArrowHalfWidth = 4.5;

// Левая граница условного носителя распределения,
// которая используется в специальных режимах рисования.
constexpr double kSupportLeft = 0.0;

// Правая граница условного носителя распределения.
constexpr double kSupportRight = 1.0;

// ----------------------------------------------------------------------------
// ФУНКЦИИ ДЛЯ НАСТРОЙКИ ДЕЛЕНИЙ ОСЕЙ
// ----------------------------------------------------------------------------
// Эти функции помогают выбрать "красивые" и стабильные шаги делений,
// чтобы оси не дергались и оставались читаемыми при zoom/pan.
// ----------------------------------------------------------------------------

// Вычисляет "красивый" шаг делений оси для заданного диапазона span
// и желаемого числа крупных делений.
double NiceTickStep(double span, int targetTicks);

// Определяет, сколько знаков после запятой нужно показывать
// для заданного шага делений.
int DecimalsForStep(double step);

// Вычисляет опорную точку (anchor) для динамических делений оси,
// чтобы сетка начиналась с устойчивого и логичного значения.
double TickAnchorForRange(double minValue, double step);

// Возвращает стабильный шаг делений, по возможности не меняющийся слишком
// резко относительно предыдущего шага при небольших изменениях диапазона.
double StableTickStep(double previousStep, double span, int targetTicks);

// Форматирует значение для hover-подсказки графика.
QString FormatHoverValue(double value);

// ----------------------------------------------------------------------------
// ФУНКЦИЯ InterpolateSeriesValueAtX
// ----------------------------------------------------------------------------
// Назначение:
// Восстановить значение серии на произвольной координате X.
//
// Функция поддерживает два режима:
// - Linear : линейная интерполяция между соседними точками;
// - Step   : ступенчатое восстановление по последнему значению слева.
//
// Возвращает:
// - std::optional<double> с найденным значением;
// - std::nullopt, если корректное значение восстановить нельзя.
// ----------------------------------------------------------------------------
std::optional<double> InterpolateSeriesValueAtX(const QVector<QPointF>& points,
                                                bool monotonicByX,
                                                PlotSeriesData::HoverMode hoverMode,
                                                double xValue);

// ----------------------------------------------------------------------------
// ФУНКЦИИ ДЛЯ ПОДГОТОВКИ НАБОРА ТОЧЕК К ОТОБРАЖЕНИЮ
// ----------------------------------------------------------------------------
// Эти функции помогают сократить объем реальной отрисовки, не теряя важную
// форму графика и сохраняя отзывчивость интерфейса.
// ----------------------------------------------------------------------------

// Прореживает набор точек, если их слишком много для прямой отрисовки.
QVector<QPointF> DownsamplePoints(const QVector<QPointF>& points);

// Проверяет, упорядочены ли точки по координате X.
// Это важно для корректной и быстрой интерполяции.
bool IsMonotonicByX(const QVector<QPointF>& points);

// Строит реально отображаемый набор точек для текущего видимого окна по X.
// При необходимости отсекает лишнее и выполняет прореживание.
QVector<QPointF> BuildRenderedPoints(const QVector<QPointF>& fullPoints,
                                     bool monotonicByX,
                                     double visibleXMin,
                                     double visibleXMax);
} // namespace PlotChartUtils
