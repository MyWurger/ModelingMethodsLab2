// Назначение файла:
// 1) объявить набор небольших функций форматирования,
//    которые используются в разных частях GUI;
// 2) централизовать логику подготовки строк для отображения в интерфейсе;
// 3) избежать дублирования однотипного форматирования чисел, индексов
//    и заголовков компонент.
//
// По роли это utility-модуль presentation-слоя:
// здесь нет тяжелой логики моделирования и нет собственных виджетов,
// а только операции подготовки текста к красивому показу в GUI.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// QString - основной тип строк Qt.
#include <QString>
// QStringList нужен для списков заголовков компонент и индексов.
#include <QStringList>
// std::size_t используется для размерности и числа компонент.
#include <cstddef>

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FormatNumericValue
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать вещественное число в аккуратную строку для GUI.
//
// Обычно такая функция нужна, чтобы:
// - не показывать лишние хвосты после запятой;
// - сохранить единый стиль числового отображения по всему приложению.
// ----------------------------------------------------------------------------
QString FormatNumericValue(double value);

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ToSubscriptDigits
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать обычные цифры в их подстрочный Unicode-вариант.
//
// Это используется для визуально более математических подписей вида x₁, x₂,
// u₁, u₂ и т.д.
// ----------------------------------------------------------------------------
QString ToSubscriptDigits(const QString& text);

// ----------------------------------------------------------------------------
// ФУНКЦИЯ IndexedSymbol
// ----------------------------------------------------------------------------
// Назначение:
// Построить обозначение символа с индексами,
// например x₁, x₂, u₃ и аналогичные варианты.
//
// Принимает:
// - prefix  : основной символ;
// - indices : строка индексов.
// ----------------------------------------------------------------------------
QString IndexedSymbol(const QString& prefix, const QString& indices);

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BuildComponentHeaders
// ----------------------------------------------------------------------------
// Назначение:
// Построить список заголовков компонент фиксированной размерности.
//
// Пример:
// prefix = "x", dimension = 4
// -> ["x₁", "x₂", "x₃", "x₄"]
//
// Это удобно для единообразного создания заголовков таблиц, матриц и графиков.
// ----------------------------------------------------------------------------
QStringList BuildComponentHeaders(const QString& prefix, std::size_t dimension);
