// Назначение файла:
// 1) объявить небольшие вспомогательные функции presentation-слоя;
// 2) централизовать правила форматирования чисел для таблиц, карточек и графиков;
// 3) собирать математически более аккуратные подписи компонент с индексами.
//
// Здесь нет логики моделирования или вычислений.
// Этот заголовок отвечает только за то, как уже посчитанные данные
// должны выглядеть в интерфейсе.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// QString нужен для строковых представлений чисел, индексов и обозначений.
#include <QString>
// QStringList нужен для списков заголовков компонент.
#include <QStringList>
// std::size_t используется для задания размерности набора компонент.
#include <cstddef>

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FormatNumericValue
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать вещественное число в компактную и читаемую строку
// для показа в GUI.
//
// Эта функция применяется в таблицах, матричных карточках и прочих местах,
// где одно и то же число должно отображаться единообразно.
//
// Принимает:
// - value : вещественное значение, которое нужно подготовить к показу.
//
// Возвращает:
// - QString : строку с учетом правил форматирования интерфейса.
// ----------------------------------------------------------------------------
QString FormatNumericValue(double value);

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ToSubscriptDigits
// ----------------------------------------------------------------------------
// Назначение:
// Заменить обычные цифры в строке на их подстрочные Unicode-аналоги.
//
// Это позволяет получать подписи вида x₁, x₂, k₁₁ и т.д.
// без HTML-разметки и без дополнительных виджетов.
//
// Принимает:
// - text : строка, в которой цифры нужно перевести в подстрочный вид.
//
// Возвращает:
// - QString : строку с подстрочными индексами.
// ----------------------------------------------------------------------------
QString ToSubscriptDigits(const QString& text);

// ----------------------------------------------------------------------------
// ФУНКЦИЯ IndexedSymbol
// ----------------------------------------------------------------------------
// Назначение:
// Построить обозначение символа с индексом в математически аккуратном виде.
//
// Примеры:
// - prefix = "x", indices = "3"  -> "x₃"
// - prefix = "k", indices = "12" -> "k₁₂"
//
// Принимает:
// - prefix  : базовое обозначение символа;
// - indices : индексная часть в обычном текстовом виде.
//
// Возвращает:
// - QString : объединенную строку вида "символ + подстрочный индекс".
// ----------------------------------------------------------------------------
QString IndexedSymbol(const QString& prefix, const QString& indices);

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BuildComponentHeaders
// ----------------------------------------------------------------------------
// Назначение:
// Построить список заголовков компонент фиксированной размерности.
//
// Эта функция нужна там, где требуется автоматически получить набор подписей:
// x₁, x₂, x₃, ...
// u₁, u₂, u₃, ...
//
// Принимает:
// - prefix    : буквенное обозначение компоненты;
// - dimension : количество компонент.
//
// Возвращает:
// - QStringList : список готовых заголовков для указанной размерности.
// ----------------------------------------------------------------------------
QStringList BuildComponentHeaders(const QString& prefix, std::size_t dimension);
