// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем общие численные типы проекта:
// NumericVector и NumericMatrix.
#include "SamplingTypes.h"

// std::size_t нужен для хранения размерности случайного вектора.
#include <cstddef>

// ----------------------------------------------------------------------------
// ПРОСТРАНСТВО ИМЕН Lab2Variant
// ----------------------------------------------------------------------------
// Назначение:
// Сгруппировать в одном месте все данные конкретного варианта лабораторной.
//
// Это пространство имен используется как единый источник "теоретической
// правды" для всего проекта:
// - GUI берет отсюда параметры для вкладки варианта;
// - вычислительное ядро берет отсюда размерность, вектор среднего
//   и матрицу ковариации;
// - код остается согласованным, потому что данные варианта не дублируются
//   в нескольких местах.
// ----------------------------------------------------------------------------
namespace Lab2Variant
{
// Размерность случайного вектора X из текущего варианта лабораторной.
constexpr std::size_t kDimension = 4;

// ----------------------------------------------------------------------------
// ФУНКЦИЯ MeanVector
// ----------------------------------------------------------------------------
// Назначение:
// Вернуть теоретический вектор математического ожидания m_X
// для заданного варианта.
//
// Возвращает:
// - NumericVector размерности kDimension.
//
// Почему функция, а не глобальная переменная:
// Такой интерфейс проще использовать как "источник значения",
// не раскрывая детали хранения и не создавая лишних проблем с инициализацией.
// ----------------------------------------------------------------------------
inline NumericVector MeanVector()
{
    // Возвращаем фиксированный вектор среднего из условия варианта.
    return {1.0, 0.0, 1.0, 0.0};
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ CovarianceMatrix
// ----------------------------------------------------------------------------
// Назначение:
// Вернуть теоретическую матрицу ковариации K_X для заданного варианта.
//
// Возвращает:
// - NumericMatrix размерности kDimension x kDimension.
//
// Практический смысл:
// Именно эта матрица затем используется для:
// - построения фактора Холецкого A;
// - генерации коррелированных наблюдений X = A * U + m_X;
// - сравнения с выборочными оценками после моделирования.
// ----------------------------------------------------------------------------
inline NumericMatrix CovarianceMatrix()
{
    // Возвращаем симметричную матрицу ковариации из условия варианта.
    return {
        {3.0, 2.0, 1.0, 0.0},
        {2.0, 8.0, 3.0, 0.0},
        {1.0, 3.0, 4.0, 0.0},
        {0.0, 0.0, 0.0, 9.0},
    };
}
} // namespace Lab2Variant
