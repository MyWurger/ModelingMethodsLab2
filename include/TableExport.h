// ============================================================================
// ФАЙЛ TABLEEXPORT.H - ЭКСПОРТ ТАБЛИЦ LAB_2 В PNG
// ============================================================================
// Назначение файла:
// 1) объявить внешний интерфейс экспорта табличных виджетов в PNG;
// 2) отделить логику сохранения изображений таблиц от MainWindow;
// 3) дать единый reusable-механизм для всех карточек, где требуется
//    выгрузка табличного результата в графический файл.
//
// По архитектурной роли это маленький служебный модуль GUI-слоя:
// он не строит таблицы и не управляет данными, а только берет уже
// существующий QTableView и сохраняет его визуальное представление в PNG.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// Предварительное объявление QString для заголовка экспортируемой таблицы.
class QString;
// Предварительное объявление QTableView - источника данных для экспорта.
class QTableView;
// Предварительное объявление QWidget - родителя для диалогов и сообщений.
class QWidget;

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ExportTableViewPng
// ----------------------------------------------------------------------------
// Назначение:
// Сохранить содержимое табличной карточки в PNG-файл.
//
// Принимает:
// - parent      : родительский виджет Qt, относительно которого показываются
//                 диалоги выбора пути и сообщения пользователю;
// - sourceTable : таблица-источник, которую нужно экспортировать;
// - title       : заголовок таблицы, используемый в имени файла
//                 и/или в экспортируемом изображении.
//
// Практический смысл:
// GUI вызывает эту функцию из карточек результатов, когда пользователь
// нажимает кнопку "Сохранить PNG".
// ----------------------------------------------------------------------------
void ExportTableViewPng(QWidget* parent, QTableView* sourceTable, const QString& title);
