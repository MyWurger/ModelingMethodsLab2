// Защита от повторного включения заголовочного файла.
#pragma once

// std::size_t нужен для размерностей, индексов и числа наблюдений.
#include <cstddef>
// std::uint32_t используется для seed генератора случайных чисел.
#include <cstdint>
// std::string нужен для текстового сообщения о результате расчета.
#include <string>
// std::vector используется как базовый контейнер для всех численных данных.
#include <vector>

// ----------------------------------------------------------------------------
// БАЗОВЫЕ ЧИСЛЕННЫЕ ТИПЫ
// ----------------------------------------------------------------------------
// Эти alias-ы задают общий "язык данных" проекта.
// Они используются и в вычислительном ядре, и в presentation-слое,
// и в GUI-обертках над матрицами и таблицами.
// ----------------------------------------------------------------------------

// Вектор вещественных чисел.
using NumericVector = std::vector<double>;
// Матрица вещественных чисел как вектор строк.
using NumericMatrix = std::vector<NumericVector>;

// ----------------------------------------------------------------------------
// СТРУКТУРА SamplingOptions
// ----------------------------------------------------------------------------
// Назначение:
// Хранить все входные параметры моделирования,
// которые задает пользователь перед запуском расчета.
//
// Эти параметры определяют:
// - объем генерируемой выборки;
// - детализацию гистограмм;
// - размер preview-результатов;
// - seed генератора случайных чисел.
// ----------------------------------------------------------------------------
struct SamplingOptions
{
    // Полный размер моделируемой выборки X.
    std::size_t sampleSize = 20000;
    // Количество интервалов разбиения для гистограмм компонент.
    std::size_t histogramBins = 24;
    // Число первых наблюдений, которые нужно сохранить для preview-таблиц
    // и траекторий компонент.
    std::size_t previewCount = 400;
    // Seed генератора случайных чисел для воспроизводимости результата.
    std::uint32_t seed = 20260414u;
};

// ----------------------------------------------------------------------------
// СТРУКТУРА HistogramBin
// ----------------------------------------------------------------------------
// Назначение:
// Описывать один интервал гистограммы для конкретной компоненты.
//
// Эта структура хранит как "геометрию" интервала, так и статистику,
// нужную для показа гистограммы в виде плотности.
// ----------------------------------------------------------------------------
struct HistogramBin
{
    // Левая граница интервала.
    double left = 0.0;
    // Правая граница интервала.
    double right = 0.0;
    // Число наблюдений, попавших в интервал.
    std::size_t count = 0;
    // Высота столбца в терминах оценки плотности распределения.
    double density = 0.0;
};

// ----------------------------------------------------------------------------
// СТРУКТУРА ComponentStatistics
// ----------------------------------------------------------------------------
// Назначение:
// Хранить основные теоретические и эмпирические характеристики
// одной компоненты случайного вектора X.
//
// Такая структура удобна для точечного анализа каждой компоненты:
// среднее, дисперсия, стандартное отклонение и диапазон значений.
// ----------------------------------------------------------------------------
struct ComponentStatistics
{
    // Индекс компоненты X_i.
    std::size_t componentIndex = 0;
    // Теоретическое математическое ожидание компоненты.
    double theoreticalMean = 0.0;
    // Теоретическая дисперсия компоненты.
    double theoreticalVariance = 0.0;
    // Выборочная оценка математического ожидания.
    double empiricalMean = 0.0;
    // Выборочная оценка дисперсии.
    double empiricalVariance = 0.0;
    // Выборочная оценка стандартного отклонения.
    double empiricalStandardDeviation = 0.0;
    // Минимальное значение компоненты в выборке.
    double minimum = 0.0;
    // Максимальное значение компоненты в выборке.
    double maximum = 0.0;
};

// ----------------------------------------------------------------------------
// СТРУКТУРА ComponentHistogram
// ----------------------------------------------------------------------------
// Назначение:
// Хранить все данные, необходимые для построения гистограммы
// одной компоненты X и наложения на нее теоретической плотности.
// ----------------------------------------------------------------------------
struct ComponentHistogram
{
    // Индекс компоненты X_i.
    std::size_t componentIndex = 0;
    // Теоретическое математическое ожидание компоненты.
    double theoreticalMean = 0.0;
    // Теоретическая дисперсия компоненты.
    double theoreticalVariance = 0.0;
    // Набор интервалов гистограммы.
    std::vector<HistogramBin> bins;
};

// ----------------------------------------------------------------------------
// СТРУКТУРА SamplingSummary
// ----------------------------------------------------------------------------
// Назначение:
// Хранить краткую сводку по результату моделирования,
// удобную для показа на вкладке итогов.
//
// Это уже не "сырые данные", а агрегированные показатели качества:
// ошибки оценки среднего, ковариации и общая матричная ошибка.
// ----------------------------------------------------------------------------
struct SamplingSummary
{
    // Размерность случайного вектора X.
    std::size_t dimension = 0;
    // Реальный размер сгенерированной выборки.
    std::size_t sampleSize = 0;
    // Максимальная по компонентам ошибка оценки среднего.
    double maxMeanError = 0.0;
    // Максимальная ошибка по элементам ковариационной матрицы.
    double maxCovarianceError = 0.0;
    // Ошибка ковариационной матрицы в норме Фробениуса.
    double frobeniusCovarianceError = 0.0;
};

// ----------------------------------------------------------------------------
// СТРУКТУРА SamplingResult
// ----------------------------------------------------------------------------
// Назначение:
// Представить полный результат работы вычислительного ядра.
//
// Это центральная структура обмена между вычислительным слоем и GUI.
// Она содержит:
// - служебный статус выполнения;
// - параметры запуска;
// - теоретические матрицы и векторы;
// - выборочные оценки;
// - статистику компонент;
// - гистограммы;
// - полную выборку и preview-выборки;
// - агрегированную summary-информацию.
// ----------------------------------------------------------------------------
struct SamplingResult
{
    // Признак успешности расчета.
    bool success = false;
    // Текстовое сообщение об успехе или ошибке.
    std::string message;
    // Параметры, с которыми был выполнен расчет.
    SamplingOptions options;

    // Теоретический вектор математического ожидания m_X.
    NumericVector theoreticalMean;
    // Теоретическая матрица ковариации K_X.
    NumericMatrix theoreticalCovariance;
    // Теоретическая матрица корреляции R_X.
    NumericMatrix theoreticalCorrelation;
    // Фактор Холецкого A, такой что K_X = A * A^T.
    NumericMatrix choleskyFactor;

    // Выборочная оценка среднего.
    NumericVector empiricalMean;
    // Выборочная оценка ковариационной матрицы.
    NumericMatrix empiricalCovariance;
    // Выборочная оценка корреляционной матрицы.
    NumericMatrix empiricalCorrelation;

    // Подробная статистика по каждой компоненте X_i.
    std::vector<ComponentStatistics> componentStatistics;
    // Гистограммы всех компонент.
    std::vector<ComponentHistogram> histograms;

    // Полная сгенерированная выборка X.
    NumericMatrix samples;
    // Первые независимые наблюдения U, использованные для preview.
    NumericMatrix independentPreviewSamples;
    // Первые преобразованные наблюдения X, использованные для preview.
    NumericMatrix transformedPreviewSamples;

    // Краткая сводка ошибок и размеров для вкладки итогов.
    SamplingSummary summary;
};
