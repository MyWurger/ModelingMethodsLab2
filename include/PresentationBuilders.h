// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем общие численные типы проекта и структуры результата.
#include "SamplingTypes.h"

// QPointF нужен для подготовки наборов точек графиков.
#include <QPointF>
// QVector используется для хранения серий точек в формате Qt.
#include <QVector>

// std::size_t используется для индексации компонент и размерностей.
#include <cstddef>
// std::vector нужен для работы с гистограммами, приходящими из вычислительного слоя.
#include <vector>

// ----------------------------------------------------------------------------
// ФУНКЦИИ ПОДГОТОВКИ МАТРИЧНЫХ ПРЕДСТАВЛЕНИЙ
// ----------------------------------------------------------------------------

// Преобразует вектор в матрицу из одной строки.
//
// Это удобно, когда GUI-виджет матриц ожидает NumericMatrix,
// а нужно показать одномерный объект в том же визуальном стиле.
NumericMatrix MakeSingleRowMatrix(const NumericVector& vector);

// Строит preview-матрицу для первых наблюдений,
// которая затем используется в табличном показе U и X.
NumericMatrix BuildPreviewMatrix(const SamplingResult& result);

// ----------------------------------------------------------------------------
// ФУНКЦИИ ПОДГОТОВКИ СЕРИЙ ТОЧЕК ДЛЯ ГРАФИКОВ
// ----------------------------------------------------------------------------

// Строит ломаную линию-контур гистограммы по набору интервалов HistogramBin.
// Возвращаемый набор точек затем используется как серия для PlotChartWidget.
QVector<QPointF> BuildHistogramOutline(const std::vector<HistogramBin>& bins);

// Строит серию точек теоретической нормальной плотности
// для конкретной компоненты гистограммы.
QVector<QPointF> BuildNormalDensityCurve(const ComponentHistogram& histogram);

// Строит траекторию одной компоненты X по первым наблюдениям выборки.
// По сути это преобразование строки/столбца previewSamples в графическую серию.
QVector<QPointF> BuildSequenceSeries(const NumericMatrix& previewSamples, std::size_t componentIndex);
