// Подключаем собственный заголовок с объявлениями реализуемых классов.
#include "TableModels.h"

// Lab2Variant нужен для фиксированной размерности варианта.
#include "Lab2Variant.h"
// UiFormatting содержит единые правила строкового представления чисел и индексов.
#include "UiFormatting.h"

// std::move используется, чтобы передавать матрицы и списки заголовков
// без лишнего копирования.
#include <utility>

// ----------------------------------------------------------------------------
// КОНСТРУКТОР NumericMatrixModel
// ----------------------------------------------------------------------------
// Просто передаем parent в базовый QAbstractTableModel.
// Собственной сложной инициализации у модели нет.
//
// Принимает:
// - parent : родительский QObject для стандартной иерархии владения Qt.
//
// Возвращает:
// - объект NumericMatrixModel в сконструированном состоянии.
// ----------------------------------------------------------------------------
NumericMatrixModel::NumericMatrixModel(QObject* parent) : QAbstractTableModel(parent) {}

// ----------------------------------------------------------------------------
// МЕТОД SetMatrixData
// ----------------------------------------------------------------------------
// Назначение:
// Полностью заменить текущее содержимое модели новой матрицей и новыми заголовками.
//
// beginResetModel()/endResetModel() обязательны для Qt, чтобы представления
// корректно поняли, что структура данных изменилась целиком.
//
// Принимает:
// - values        : новую матрицу значений;
// - columnHeaders : новые подписи столбцов;
// - rowHeaders    : новые подписи строк.
//
// Возвращает:
// - void : метод обновляет внутреннее состояние модели.
// ----------------------------------------------------------------------------
void NumericMatrixModel::SetMatrixData(NumericMatrix values, QStringList columnHeaders, QStringList rowHeaders)
{
    // Уведомляем представления, что сейчас произойдет полный сброс модели.
    beginResetModel();

    // Сохраняем новую матрицу внутри модели.
    values_ = std::move(values);

    // Сохраняем новые подписи столбцов.
    columnHeaders_ = std::move(columnHeaders);

    // Сохраняем новые подписи строк.
    rowHeaders_ = std::move(rowHeaders);

    // Завершаем сброс и разрешаем QTableView перечитать модель заново.
    endResetModel();
}

// ----------------------------------------------------------------------------
// МЕТОД rowCount
// ----------------------------------------------------------------------------
// Возвращает число строк матрицы.
//
// Если parent валиден, значит Qt спрашивает количество дочерних элементов
// у уже существующей ячейки. Для обычной плоской таблицы дочерней иерархии нет,
// поэтому в таком случае нужно вернуть 0.
//
// Принимает:
// - parent : индекс родительского элемента, который Qt передает по своему API.
//
// Возвращает:
// - int : количество строк матрицы или 0 для вложенного запроса.
// ----------------------------------------------------------------------------
int NumericMatrixModel::rowCount(const QModelIndex& parent) const
{
    // Для древовидных моделей здесь мог бы быть другой сценарий,
    // но наша модель плоская, поэтому у ячеек нет дочерних элементов.
    if (parent.isValid())
    {
        return 0;
    }

    // Количество строк совпадает с числом строк хранимой матрицы.
    return static_cast<int>(values_.size());
}

// ----------------------------------------------------------------------------
// МЕТОД columnCount
// ----------------------------------------------------------------------------
// Возвращает число столбцов матрицы.
//
// Если матрица еще не задана, количество столбцов равно 0.
//
// Принимает:
// - parent : индекс родительского элемента из API Qt.
//
// Возвращает:
// - int : количество столбцов матрицы или 0, если данных еще нет.
// ----------------------------------------------------------------------------
int NumericMatrixModel::columnCount(const QModelIndex& parent) const
{
    // Для вложенных узлов число столбцов не определяется,
    // потому что модель не имеет древовидной структуры.
    if (parent.isValid() || values_.empty())
    {
        return 0;
    }

    // Предполагается, что матрица прямоугольная, поэтому число столбцов
    // берем по первой строке.
    return static_cast<int>(values_.front().size());
}

// ----------------------------------------------------------------------------
// МЕТОД data
// ----------------------------------------------------------------------------
// Возвращает данные конкретной ячейки для указанной роли Qt.
//
// Здесь нас интересуют две роли:
// - Qt::DisplayRole       : что именно показать пользователю;
// - Qt::TextAlignmentRole : как выровнять содержимое ячейки.
//
// Принимает:
// - index : индекс ячейки, данные которой запрашивает представление;
// - role  : роль Qt, определяющая, какие именно данные нужны.
//
// Возвращает:
// - QVariant : текст значения, выравнивание или пустой результат для неподдерживаемых ролей.
// ----------------------------------------------------------------------------
QVariant NumericMatrixModel::data(const QModelIndex& index, int role) const
{
    // Сначала отсекаем некорректный индекс.
    // Такое может происходить, когда представление Qt обращается к модели
    // с индексом вне допустимого диапазона или с техническим "пустым" индексом.
    // В этом случае модель обязана безопасно вернуть пустой QVariant,
    // а не пытаться читать values_ по несуществующим координатам.
    if (!index.isValid())
    {
        // Пустой QVariant означает для Qt: "по этому запросу данных нет".
        return {};
    }

    // Далее проверяем, какую именно роль запросило представление.
    // Для одной и той же ячейки Qt может многократно вызывать data(...)
    // с разными role: отдельно за текстом, отдельно за выравниванием,
    // отдельно за цветом, шрифтом и т.д.
    if (role == Qt::DisplayRole)
    {
        // Для роли отображения нужно вернуть именно текстовое содержимое ячейки,
        // которое пользователь увидит в таблице.
        //
        // Математически здесь нет дополнительного преобразования матрицы:
        // мы просто берем элемент a_{ij} по координатам (row, column)
        // из уже подготовленной числовой матрицы.
        //
        // Затем этот числовой элемент переводится в строку через общий форматтер,
        // чтобы все матричные объекты GUI отображались единообразно:
        // без шумовых хвостов, без "−0" и с аккуратной записью больших чисел.
        return FormatNumericValue(values_[index.row()][index.column()]);
    }

    // Если Qt спрашивает не текст ячейки, а правило ее визуального размещения,
    // обрабатываем роль выравнивания отдельно.
    if (role == Qt::TextAlignmentRole)
    {
        // Центрирование по горизонтали и вертикали делает матричную таблицу
        // визуально ближе к классической математической записи,
        // где элементы воспринимаются как ровная прямоугольная структура.
        return Qt::AlignCenter;
    }

    // Если запрошена любая другая роль, например DecorationRole, ToolTipRole,
    // ForegroundRole и т.п., эта модель ее специально не обслуживает.
    // Поэтому снова возвращаем пустой QVariant и оставляем Qt использовать
    // стандартное поведение без дополнительных эффектов.
    return {};
}

// ----------------------------------------------------------------------------
// МЕТОД headerData
// ----------------------------------------------------------------------------
// Возвращает подписи заголовков строк и столбцов.
//
// При наличии явных заголовков используем их.
// Если заголовок для конкретной секции не задан, подставляем запасной номер.
//
// Принимает:
// - section     : номер секции заголовка;
// - orientation : ориентация заголовка, горизонтальная или вертикальная;
// - role        : роль Qt, для которой запрашиваются данные заголовка.
//
// Возвращает:
// - QVariant : текст заголовка либо пустой результат для неподдерживаемых ролей.
// ----------------------------------------------------------------------------
QVariant NumericMatrixModel::headerData(int section, Qt::Orientation orientation, int role) const
{
    // Заголовочный текст отдаем только для роли отображения.
    if (role != Qt::DisplayRole)
    {
        return {};
    }

    // Сначала обрабатываем горизонтальные заголовки.
    if (orientation == Qt::Horizontal)
    {
        // Если пользовательские подписи столбцов есть, используем их напрямую.
        if (section >= 0 && section < columnHeaders_.size())
        {
            return columnHeaders_[section];
        }

        // Резервный вариант для случая, когда заголовки не были заданы.
        return QString::number(section + 1);
    }

    // Если дошли сюда, значит речь идет о вертикальном заголовке.
    if (section >= 0 && section < rowHeaders_.size())
    {
        // Для строк сначала пытаемся отдать пользовательские подписи.
        return rowHeaders_[section];
    }

    // Если строковые заголовки не заданы, возвращаем простой номер строки.
    return QString::number(section + 1);
}

// ----------------------------------------------------------------------------
// КОНСТРУКТОР SampleTableModel
// ----------------------------------------------------------------------------
// Модель полной выборки хранит только ссылку на внешние данные,
// поэтому в конструкторе тоже нет дополнительной логики.
//
// Принимает:
// - parent : родительский QObject для стандартной модели владения Qt.
//
// Возвращает:
// - объект SampleTableModel в сконструированном состоянии.
// ----------------------------------------------------------------------------
SampleTableModel::SampleTableModel(QObject* parent) : QAbstractTableModel(parent) {}

// ----------------------------------------------------------------------------
// МЕТОД SetSamples
// ----------------------------------------------------------------------------
// Назначение:
// Подключить к модели внешнюю матрицу выборки.
//
// Здесь не происходит копирование данных: модель лишь запоминает указатель
// на уже существующую матрицу, которой владеет другой слой приложения.
//
// Принимает:
// - samples : указатель на внешнюю матрицу выборки.
//
// Возвращает:
// - void : метод обновляет внутреннюю ссылку модели на данные.
// ----------------------------------------------------------------------------
void SampleTableModel::SetSamples(const NumericMatrix* samples)
{
    // Полный сброс нужен, потому что может поменяться как число строк,
    // так и сами значения внутри таблицы.
    beginResetModel();

    // Запоминаем адрес внешней матрицы без копирования самих данных.
    samples_ = samples;

    // Сообщаем представлениям, что теперь можно заново читать модель.
    endResetModel();
}

// ----------------------------------------------------------------------------
// МЕТОД rowCount
// ----------------------------------------------------------------------------
// Возвращает число наблюдений в выборке.
//
// Принимает:
// - parent : индекс родительского элемента из API Qt.
//
// Возвращает:
// - int : количество строк выборки или 0, если данных еще нет.
// ----------------------------------------------------------------------------
int SampleTableModel::rowCount(const QModelIndex& parent) const
{
    // Для вложенных элементов и при отсутствии выборки строк не существует.
    if (parent.isValid() || samples_ == nullptr)
    {
        return 0;
    }

    // Каждая строка выборки соответствует одному наблюдению X.
    return static_cast<int>(samples_->size());
}

// ----------------------------------------------------------------------------
// МЕТОД columnCount
// ----------------------------------------------------------------------------
// Возвращает число столбцов таблицы выборки.
//
// Дополнительный первый столбец нужен под номер наблюдения.
// Поэтому итоговое число столбцов равно:
// 1 + размерность вектора X.
//
// Принимает:
// - parent : индекс родительского элемента из API Qt.
//
// Возвращает:
// - int : число столбцов таблицы выборки.
// ----------------------------------------------------------------------------
int SampleTableModel::columnCount(const QModelIndex& parent) const
{
    // Плоская таблица не имеет дочерних уровней,
    // поэтому для вложенных запросов возвращаем 0.
    if (parent.isValid())
    {
        return 0;
    }

    // Первый столбец - номер строки, остальные столбцы - компоненты X.
    return static_cast<int>(Lab2Variant::kDimension + 1);
}

// ----------------------------------------------------------------------------
// МЕТОД data
// ----------------------------------------------------------------------------
// Возвращает содержимое ячейки полной выборки.
//
// Столбец 0 - это порядковый номер наблюдения.
// Остальные столбцы - компоненты x₁, x₂, ..., xₙ соответствующей строки выборки.
//
// Принимает:
// - index : индекс ячейки, которую требуется отобразить;
// - role  : роль Qt, определяющая тип требуемых данных.
//
// Возвращает:
// - QVariant : номер строки, значение компоненты, выравнивание или пустой результат.
// ----------------------------------------------------------------------------
QVariant SampleTableModel::data(const QModelIndex& index, int role) const
{
    // Сначала проверяем базовую корректность запроса.
    // Если индекс невалиден или сама матрица выборки еще не подключена,
    // модель не имеет права читать данные и должна сразу вернуть "пусто".
    if (!index.isValid() || samples_ == nullptr)
    {
        // Пустой QVariant здесь означает отсутствие доступных данных для данного запроса.
        return {};
    }

    // Как и в любой Qt-модели, дальше поведение зависит от роли запроса.
    // Для одной и той же ячейки представление может последовательно спросить:
    // что показывать, как выравнивать, каким цветом рисовать и так далее.
    if (role == Qt::DisplayRole)
    {
        // Для роли отображения нужно вернуть видимое содержимое ячейки.
        // При этом у таблицы выборки первый столбец служебный:
        // он не хранит компоненту X, а показывает номер наблюдения.
        if (index.column() == 0)
        {
            // Нумерация для пользователя начинается с 1, а не с 0.
            return index.row() + 1;
        }

        // Для всех остальных столбцов берем соответствующую компоненту
        // из строки выборки и форматируем ее по общим правилам проекта.
        //
        // С математической точки зрения каждая строка samples_[k]
        // представляет одно наблюдение случайного вектора X,
        // а каждый столбец после первого - это одна из его компонент x_1, x_2, ..., x_n.
        // Вычитание единицы нужно потому, что column 0 уже занят колонкой "№".
        return FormatNumericValue((*samples_)[index.row()][index.column() - 1]);
    }

    // Если Qt запрашивает не текст, а способ выравнивания содержимого,
    // возвращаем отдельное правило специально для этой роли.
    if (role == Qt::TextAlignmentRole)
    {
        // Центрирование выравнивает и номер строки, и все компоненты X,
        // благодаря чему таблица воспринимается как единая числовая сетка.
        return Qt::AlignCenter;
    }

    // Прочие роли эта модель специально не реализует.
    // Возвращаем пустой QVariant и не навязываем представлению
    // никаких дополнительных декоративных данных.
    return {};
}

// ----------------------------------------------------------------------------
// МЕТОД headerData
// ----------------------------------------------------------------------------
// Возвращает горизонтальные заголовки таблицы выборки.
//
// Вертикальные заголовки здесь специально не используются:
// номер наблюдения уже вынесен в первый столбец.
//
// Принимает:
// - section     : номер секции заголовка;
// - orientation : ориентация заголовка;
// - role        : роль Qt, для которой требуются данные заголовка.
//
// Возвращает:
// - QVariant : текст заголовка столбца либо пустой результат.
// ----------------------------------------------------------------------------
QVariant SampleTableModel::headerData(int section, Qt::Orientation orientation, int role) const
{
    // Заголовочный текст нужен только для роли отображения.
    if (role != Qt::DisplayRole)
    {
        return {};
    }

    // Вертикальные заголовки у этой модели не используются.
    if (orientation == Qt::Horizontal)
    {
        // Первый столбец не хранит компоненту X, а отображает номер наблюдения.
        if (section == 0)
        {
            return "№";
        }

        // Остальные столбцы - подписи компонент x₁, x₂, ..., xₙ.
        return BuildComponentHeaders("x", Lab2Variant::kDimension).value(section - 1);
    }

    // Вертикальные заголовки не показываем.
    return {};
}
