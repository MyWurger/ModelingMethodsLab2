// ----------------------------------------------------------------------------
// ФУНКЦИЯ NiceTickStep
// ----------------------------------------------------------------------------
// Назначение:
// Выбрать "красивый" шаг делений оси по текущему диапазону span.
//
// Вместо произвольного шага функция старается выбрать значения из набора
// 1, 2, 5, 10 с учетом порядка величины.
// Это стандартный прием для построения читаемых осей графиков.
//
// Принимает:
// - span        : длину видимого диапазона по оси;
// - targetTicks : желаемое число основных делений.
//
// Возвращает:
// - double : удобный для глаза шаг сетки.
// ----------------------------------------------------------------------------
double NiceTickStep(double span, int targetTicks)
{
    // Если диапазон некорректен, не положителен или число делений бессмысленно мало,
    // возвращаем безопасный шаг по умолчанию.
    if (!std::isfinite(span) || span <= 0.0 || targetTicks < 2)
    {
        return 1.0;
    }

    // Сырой шаг - это просто диапазон, поделенный на число промежутков между делениями.
    const double rawStep = span / static_cast<double>(targetTicks - 1);

    // magnitude = 10^k задает порядок величины rawStep.
    // Например, для 37 -> 10, для 0.08 -> 0.01.
    const double magnitude = std::pow(10.0, std::floor(std::log10(rawStep)));

    // normalized показывает шаг в "нормализованной" шкале порядка единицы.
    // Например:
    // 37 / 10 = 3.7
    // 0.08 / 0.01 = 8.
    const double normalized = rawStep / magnitude;

    // Дальше округляем normalized к одному из стандартных "красивых" значений:
    // 1, 2, 5 или 10.
    // Инициализируем nice базовым значением 1.
    double nice = 1.0;

    // Если normalized ближе к 10, чем к 5, выбираем шаг 10 * magnitude.
    if (normalized >= 7.5)
    {
        nice = 10.0;
    }
    // Если normalized лежит примерно в зоне "пяти",
    // выбираем шаг 5 * magnitude.
    else if (normalized >= 3.5)
    {
        nice = 5.0;
    }
    // Если normalized лежит примерно в зоне "двух",
    // выбираем шаг 2 * magnitude.
    else if (normalized >= 1.5)
    {
        nice = 2.0;
    }

    // Возвращаем nice * magnitude, то есть красивый шаг в исходном масштабе оси.
    return nice * magnitude;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ DecimalsForStep
// ----------------------------------------------------------------------------
// Назначение:
// Определить, сколько знаков после запятой нужно показывать для данного шага оси.
//
// Принимает:
// - step : шаг основных делений.
//
// Возвращает:
// - int : количество десятичных знаков для подписей.
// ----------------------------------------------------------------------------
int DecimalsForStep(double step)
{
    // Если шаг нечисловой или неположительный, возвращаем запасной уровень точности.
    if (!std::isfinite(step) || step <= 0.0)
    {
        return 6;
    }

    // Если шаг уже не дробный, дополнительные знаки после запятой не нужны.
    if (step >= 1.0)
    {
        return 0;
    }

    // Для дробных шагов оцениваем количество знаков по порядку величины шага.
    // Например:
    // step = 0.1  -> 1-2 знака,
    // step = 0.01 -> 2-3 знака.
    // -log10(step) оценивает порядок дробности шага,
    // а +1 оставляет небольшой запас, чтобы подписи не обрезались грубо.
    const int decimals = static_cast<int>(std::ceil(-std::log10(step))) + 1;

    // Ограничиваем количество знаков сверху и снизу,
    // чтобы форматирование не уходило в абсурдную точность.
    return std::clamp(decimals, 0, 12);
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ TickAnchorForRange
// ----------------------------------------------------------------------------
// Назначение:
// Найти опорную точку сетки слева от диапазона.
//
// Принимает:
// - minValue : левая граница видимого диапазона;
// - step     : шаг делений.
//
// Возвращает:
// - double : значение, от которого удобно начинать строить сетку.
// ----------------------------------------------------------------------------
double TickAnchorForRange(double minValue, double step)
{
    // Если левая граница диапазона или шаг некорректны,
    // вычислять опорную точку сетки бессмысленно.
    if (!std::isfinite(minValue) || !(step > 0.0) || !std::isfinite(step))
    {
        return 0.0;
    }

    // Берем наибольшее кратное step, которое не превосходит minValue:
    // anchor = floor(minValue / step) * step.
    // Это позволяет выровнять сетку на красивых "кратных" значениях.
    return std::floor(minValue / step) * step;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ StableTickStep
// ----------------------------------------------------------------------------
// Назначение:
// Вернуть шаг делений оси с возможностью учитывать предыдущий шаг.
//
// Сейчас функция просто делегирует расчет в NiceTickStep,
// но оставляет точку расширения на будущее.
//
// Принимает:
// - previousStep : предыдущий шаг сетки;
// - span         : текущий диапазон;
// - targetTicks  : желаемое число делений.
//
// Возвращает:
// - double : шаг сетки для текущего диапазона.
// ----------------------------------------------------------------------------
double StableTickStep(double previousStep, double span, int targetTicks)
{
    // Пока предыдущий шаг реально не участвует в расчете,
    // но параметр оставлен в интерфейсе под будущую стабилизацию сетки.
    Q_UNUSED(previousStep);

    // На текущем этапе стабильный шаг совпадает с обычным "красивым" шагом.
    return NiceTickStep(span, targetTicks);
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FormatHoverValue
// ----------------------------------------------------------------------------
// Назначение:
// Подготовить значение для hover-подсказки на графике.
//
// Принимает:
// - value : числовое значение, которое нужно показать пользователю.
//
// Возвращает:
// - QString : компактную строку для tooltip/hover-лейбла.
// ----------------------------------------------------------------------------
QString FormatHoverValue(double value)
{
    // Если значение не является конечным числом,
    // показываем нейтральный маркер отсутствия корректного числа.
    if (!std::isfinite(value))
    {
        return QStringLiteral("—");
    }

    // Числа, близкие к нулю с учетом погрешности double,
    // лучше сразу показывать как "0".
    if (qFuzzyIsNull(value))
    {
        return QStringLiteral("0");
    }

    // Работать удобнее с модулем значения, чтобы выбирать формат независимо от знака.
    const double absoluteValue = std::abs(value);

    // Очень большие и очень маленькие значения показываем в экспоненциальной записи,
    // потому что фиксированный формат будет либо слишком длинным, либо нечитаемым.
    if (absoluteValue >= 1.0e4 || absoluteValue < 1.0e-4)
    {
        return QString::number(value, 'e', 3);
    }

    // Для обычного диапазона значений используем фиксированный формат с 4 знаками.
    QString text = QString::number(value, 'f', 4);

    // Затем убираем хвостовые нули и возможную оставшуюся точку,
    // чтобы подпись была компактной.
    while (text.contains('.') && (text.endsWith('0') || text.endsWith('.')))
    {
        text.chop(1);
    }

    // После округления может появиться строка "-0",
    // которую для пользователя корректнее показывать как "0".
    return text == "-0" ? QStringLiteral("0") : text;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ InterpolateSeriesValueAtX
// ----------------------------------------------------------------------------
// Назначение:
// Найти или интерполировать значение серии в заданной точке x.
//
// Это нужно для hover-режима, когда пользователь водит мышью по графику,
// а интерфейсу нужно показать значение серии в выбранной вертикали.
//
// Принимает:
// - points       : набор точек серии;
// - monotonicByX : флаг, отсортированы ли точки по возрастанию x;
// - hoverMode    : способ чтения значения, ступенчатый или линейный;
// - xValue       : интересующее значение x.
//
// Возвращает:
// - std::optional<double> : найденное/интерполированное значение y
//                           либо std::nullopt, если вычисление невозможно.
// ----------------------------------------------------------------------------
std::optional<double> InterpolateSeriesValueAtX(const QVector<QPointF>& points,
                                                bool monotonicByX,
                                                PlotSeriesData::HoverMode hoverMode,
                                                double xValue)
{
    // Если точек нет или запрошенная координата x некорректна,
    // ни о какой интерполяции речи быть не может.
    if (points.isEmpty() || !std::isfinite(xValue))
    {
        return std::nullopt;
    }

    // Отдельно обрабатываем ступенчатый hover-режим,
    // потому что там нужна не линейная интерполяция, а чтение "последнего значения слева".
    if (hoverMode == PlotSeriesData::HoverMode::Step)
    {
        // Для монотонной по x серии можно искать границу ступеньки бинарным поиском.
        if (monotonicByX)
        {
            // Для ступенчатого режима находим первую точку правее xValue.
            // Тогда значение ступеньки определяется предыдущей точкой слева.
            const auto upperIt =
                std::upper_bound(points.cbegin(), points.cend(), xValue, [](double value, const QPointF& point) {
                    // upper_bound использует этот компаратор, чтобы сравнивать xValue с point.x().
                    return value < point.x();
                });

            // Если точка вставки попала в самое начало,
            // значит xValue лежит левее или ровно у первой точки,
            // и в качестве значения ступеньки берем первый y.
            return upperIt == points.cbegin() ? std::optional<double>(points.front().y())
                                              // Иначе значение ступеньки определяется предыдущей точкой слева.
                                              : std::optional<double>((upperIt - 1)->y());
        }

        // Для неотсортированной серии вводим маленькую погрешность сравнения по x,
        // чтобы избежать артефактов на границе из-за double-ошибок.
        constexpr double kStepTolerance = 1.0e-12;

        // По умолчанию начинаем с y первой точки.
        double lastY = points.front().y();

        // Флаг нужен, чтобы понимать, нашли ли мы вообще хоть одну точку слева.
        bool foundPointOnTheLeft = false;

        // Линейно просматриваем точки, пока не уйдем правее интересующей вертикали.
        for (const QPointF& point : points)
        {
            // Ищем последнюю точку, которая лежит не правее текущей вертикали.
            if (point.x() <= xValue + kStepTolerance)
            {
                // Как только находим подходящую точку,
                // запоминаем ее значение как текущее значение ступеньки.
                lastY = point.y();
                foundPointOnTheLeft = true;
            }
            else
            {
                // Как только ушли правее, дальнейший обход в step-логике уже не нужен.
                break;
            }
        }

        // Если точка слева была найдена, возвращаем ее y.
        // Если нет, используем значение первой точки как крайний левый уровень.
        return foundPointOnTheLeft ? std::optional<double>(lastY)
                                   : std::optional<double>(points.front().y());
    }

    // Если серия не монотонна по x или в ней всего одна точка,
    // корректная линейная интерполяция по соседям невозможна или бессмысленна.
    if (!monotonicByX || points.size() == 1)
    {
        // Если серия не монотонна по x, нормальная линейная интерполяция
        // по соседям в x уже не определена однозначно.
        // В таком случае берем просто ближайшую по x точку.
        const auto nearestIt =
            std::min_element(points.cbegin(), points.cend(), [xValue](const QPointF& left, const QPointF& right) {
                // Сравниваем две точки по модулю расстояния до вертикали x = xValue.
                return std::abs(left.x() - xValue) < std::abs(right.x() - xValue);
            });

        // Если ближайшая точка найдена, возвращаем ее ординату.
        // Иначе возвращаем отсутствие результата.
        return nearestIt != points.cend() ? std::optional<double>(nearestIt->y()) : std::nullopt;
    }

    // Для монотонной серии находим первую точку справа от xValue.
    const auto rightIt =
        std::lower_bound(points.cbegin(), points.cend(), xValue, [](const QPointF& point, double value) {
            // lower_bound ищет первую точку, для которой point.x() < value уже ложно.
            return point.x() < value;
        });

    // Если правая точка совпала с началом,
    // значит xValue лежит левее всего диапазона, и возвращаем первый y.
    if (rightIt == points.cbegin())
    {
        return points.front().y();
    }

    // Если правая точка ушла за конец,
    // значит xValue лежит правее всего диапазона, и возвращаем последний y.
    if (rightIt == points.cend())
    {
        return points.back().y();
    }

    // Берем правую точку интервала интерполяции.
    const QPointF rightPoint = *rightIt;

    // Берем левую соседнюю точку того же интервала.
    const QPointF leftPoint = *(rightIt - 1);

    // dx - горизонтальное расстояние между соседними узлами интерполяции.
    const double dx = rightPoint.x() - leftPoint.x();

    // Если точки практически совпали по x,
    // обычная формула интерполяции станет численно неустойчивой.
    if (std::abs(dx) < 1.0e-12)
    {
        // Если точки почти совпали по x, классическая линейная интерполяция нестабильна.
        // В этом вырожденном случае выбираем верхнее значение как безопасный компромисс.
        return std::max(leftPoint.y(), rightPoint.y());
    }

    // Линейная интерполяция:
    // t = (x - x_left) / (x_right - x_left),
    // y = y_left + (y_right - y_left) * t.
    const double t = std::clamp((xValue - leftPoint.x()) / dx, 0.0, 1.0);

    // Возвращаем интерполированную ординату между соседними узлами.
    return leftPoint.y() + (rightPoint.y() - leftPoint.y()) * t;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ DownsamplePoints
// ----------------------------------------------------------------------------
// Назначение:
// Сократить число отображаемых точек без полного уничтожения формы графика.
//
// Принимает:
// - points : исходная серия точек.
//
// Возвращает:
// - QVector<QPointF> : прореженная серия для ускоренной отрисовки.
// ----------------------------------------------------------------------------
QVector<QPointF> DownsamplePoints(const QVector<QPointF>& points)
{
    // Если точек и так немного, прореживание не требуется.
    if (points.size() <= kMaxRenderedPoints)
    {
        return points;
    }

    // Подготавливаем контейнер прореженной серии.
    QVector<QPointF> sampled;

    // Резервируем место ровно под целевое число отображаемых точек.
    sampled.reserve(kMaxRenderedPoints);

    // Шаг по индексам выбираем так, чтобы равномерно покрыть всю серию
    // от первой до последней точки.
    const double step = static_cast<double>(points.size() - 1) / static_cast<double>(kMaxRenderedPoints - 1);

    // Последний допустимый индекс исходной серии.
    const int maxIndex = points.size() - 1;

    // Выбираем почти равномерную сетку индексов по всей длине массива.
    for (int i = 0; i < kMaxRenderedPoints - 1; ++i)
    {
        // Округляем дробный индекс до ближайшего реального индекса исходной серии.
        const int index = static_cast<int>(std::round(i * step));

        // Берем соответствующую точку исходной серии с защитой через clamp.
        sampled.append(points[std::clamp(index, 0, maxIndex)]);
    }

    // Последнюю точку добавляем отдельно, чтобы конец графика гарантированно сохранился.
    sampled.append(points.back());

    // Возвращаем прореженную серию.
    return sampled;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ IsMonotonicByX
// ----------------------------------------------------------------------------
// Назначение:
// Проверить, не убывает ли серия по координате x.
//
// Принимает:
// - points : набор точек графика.
//
// Возвращает:
// - bool : true, если x идет неубывающе, иначе false.
// ----------------------------------------------------------------------------
bool IsMonotonicByX(const QVector<QPointF>& points)
{
    // Монотонность по x здесь означает простое условие:
    // каждая следующая точка серии не должна уходить левее предыдущей.
    //
    // Формально проверяется неубывание:
    // x_i >= x_(i-1) для всех i от 1 до N-1.
    //
    // Эта проверка критична для дальнейшей быстрой геометрии:
    // если точки упорядочены по x, то по ним можно безопасно выполнять
    // бинарный поиск, интерполяцию по соседям и выборку видимого диапазона.
    for (int i = 1; i < points.size(); ++i)
    {
        // Сравниваем текущую точку с предыдущей.
        // Если x текущей точки меньше x предыдущей,
        // значит порядок по оси X нарушен.
        if (points[i].x() < points[i - 1].x())
        {
            // В этом случае серия уже не может считаться монотонной,
            // а значит быстрые алгоритмы, рассчитанные на упорядоченность, применять нельзя.
            return false;
        }
    }

    // Если ни одного нарушения порядка не найдено,
    // значит по всей серии выполняется условие x_i >= x_(i-1),
    // и серия считается монотонной по x.
    return true;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BuildRenderedPoints
// ----------------------------------------------------------------------------
// Назначение:
// Построить набор точек, который реально будет рисоваться на экране.
//
// Принимает:
// - fullPoints   : полная серия точек;
// - monotonicByX : флаг монотонности по x;
// - visibleXMin  : левая граница текущего окна по x;
// - visibleXMax  : правая граница текущего окна по x.
//
// Возвращает:
// - QVector<QPointF> : либо полная, либо обрезанная и/или прореженная серия.
// ----------------------------------------------------------------------------
QVector<QPointF> BuildRenderedPoints(const QVector<QPointF>& fullPoints,
                                     bool monotonicByX,
                                     double visibleXMin,
                                     double visibleXMax)
{
    // Первый быстрый случай:
    // если точек и так не больше лимита рендера,
    // нет смысла ничего отбрасывать или прореживать.
    // Вся серия спокойно помещается в допустимый бюджет отрисовки.
    if (fullPoints.size() <= kMaxRenderedPoints)
    {
        return fullPoints;
    }

    // Для немонотонной серии нельзя надежно вырезать видимый диапазон по x бинарным поиском,
    // потому что точки могут "возвращаться назад" по горизонтали.
    //
    // В таком случае нельзя корректно сказать, что все видимые точки лежат
    // в одном непрерывном поддиапазоне индексов.
    // Поэтому безопасный вариант здесь только один:
    // проредить всю серию целиком без попытки умного вырезания окна.
    if (!monotonicByX)
    {
        return DownsamplePoints(fullPoints);
    }

    // Для монотонной серии ситуация лучше:
    // все точки, попадающие в видимый диапазон по x,
    // образуют один непрерывный отрезок индексов.
    //
    // Поэтому вместо линейного обхода всей серии можно использовать бинарный поиск.
    const auto leftIt =
        std::lower_bound(fullPoints.cbegin(), fullPoints.cend(), visibleXMin, [](const QPointF& point, double value) {
            // lower_bound ищет первую точку, для которой условие point.x() < value
            // перестает быть истинным.
            //
            // Значит в результате получаем первую точку,
            // чья координата x уже не меньше visibleXMin.
            return point.x() < value;
        });
    const auto rightIt =
        std::upper_bound(fullPoints.cbegin(), fullPoints.cend(), visibleXMax, [](double value, const QPointF& point) {
            // upper_bound ищет первую точку, которая уже строго правее visibleXMax.
            //
            // То есть rightIt указывает на первую точку,
            // которую уже нельзя считать принадлежащей видимому диапазону.
            return value < point.x();
        });

    // Переводим найденные итераторы в целочисленные индексы.
    //
    // firstIndex - индекс первой точки, потенциально попавшей в окно.
    // lastIndex  - индекс последней точки, лежащей не правее visibleXMax.
    int firstIndex = static_cast<int>(std::distance(fullPoints.cbegin(), leftIt));
    int lastIndex = static_cast<int>(std::distance(fullPoints.cbegin(), rightIt)) - 1;

    // Чуть расширяем диапазон слева и справа на одну точку,
    // чтобы линия визуально не рвалась у границы видимого окна.
    //
    // Это важно для непрерывных линий:
    // если взять только строго внутренние точки окна,
    // сегмент на краю может быть обрезан слишком резко.
    //
    // Математически мы просто берем соседние точки по индексу,
    // чтобы сохранить контекст линии возле границы.
    firstIndex = std::max(0, firstIndex - 1);
    lastIndex = std::min(static_cast<int>(fullPoints.size()) - 1, std::max(firstIndex, lastIndex + 1));

    // Теперь готовим отдельный буфер только для нужного локального диапазона.
    QVector<QPointF> visiblePoints;

    // Резервируем место под все точки локального диапазона,
    // чтобы не плодить перераспределения при append.
    visiblePoints.reserve(lastIndex - firstIndex + 1);

    // Перекладываем нужный непрерывный фрагмент из полной серии в локальный буфер.
    // После этого в visiblePoints окажутся только точки,
    // реально влияющие на текущую картинку на экране.
    for (int index = firstIndex; index <= lastIndex; ++index)
    {
        visiblePoints.append(fullPoints[index]);
    }

    // Если локальных точек уже достаточно мало, возвращаем их как есть.
    // Иначе дополнительно прореживаем уже только видимую часть.
    //
    // Это важный момент:
    // мы не прореживаем всю исходную серию повторно,
    // а работаем только с актуальным окном.
    // Поэтому при сильном zoom график остается детальным внутри видимой области.
    return visiblePoints.size() <= kMaxRenderedPoints ? visiblePoints : DownsamplePoints(visiblePoints);
}
