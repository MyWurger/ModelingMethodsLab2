// Подключаем собственный заголовок с объявлениями функций форматирования.
#include "UiFormatting.h"

// QtGlobal нужен для qFuzzyIsNull - "мягкой" проверки числа на близость к нулю.
#include <QtGlobal>

// std::isfinite и std::abs используются для анализа вещественных значений.
#include <cmath>

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FormatNumericValue
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать вещественное число в строку для аккуратного показа в GUI.
//
// Основные правила форматирования:
// - нечисловые значения показываем явно как "не число";
// - очень маленькие значения около нуля сводим к "0";
// - слишком маленькие или слишком большие числа показываем в экспоненциальной
//   записи, чтобы строка не разъезжалась по интерфейсу;
// - обычные значения показываем в фиксированном формате,
//   но затем убираем лишние нули в конце.
//
// Принимает:
// - value : вещественное значение, которое требуется подготовить к показу.
//
// Возвращает:
// - QString : уже отформатированную строку для интерфейса.
// ----------------------------------------------------------------------------
QString FormatNumericValue(double value)
{
    // Если значение не является конечным числом (NaN или бесконечность),
    // сразу возвращаем специальную строку.
    if (!std::isfinite(value))
    {
        return "не число";
    }

    // qFuzzyIsNull проверяет, что число достаточно близко к нулю
    // с учетом особенностей вещественной арифметики.
    if (qFuzzyIsNull(value))
    {
        return "0";
    }

    // Абсолютное значение нужно для проверки диапазона без учета знака.
    const double absoluteValue = std::abs(value);

    // Для слишком маленьких и слишком больших чисел используем
    // экспоненциальную запись, чтобы текст оставался компактным и читаемым.
    if (absoluteValue < 1e-4 || absoluteValue >= 1e8)
    {
        return QString::number(value, 'e', 5);
    }

    // Для обычного диапазона сначала формируем строку в фиксированном формате
    // с запасом по точности.
    QString text = QString::number(value, 'f', 6);

    // Затем убираем лишние хвостовые нули и, если нужно, саму десятичную точку.
    // Например:
    // 1.230000 -> 1.23
    // 5.000000 -> 5
    while (text.contains('.') && (text.endsWith('0') || text.endsWith('.')))
    {
        text.chop(1);
    }

    // После округления может получиться строка "-0".
    // Для пользователя такой результат выглядит шумно, поэтому сводим его к "0".
    if (text == "-0")
    {
        return "0";
    }

    // Возвращаем уже очищенное текстовое представление числа.
    return text;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ToSubscriptDigits
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать обычные цифры в строке в их подстрочный Unicode-вариант.
//
// Пример:
// "123" -> "₁₂₃"
//
// Это используется для математически более аккуратных обозначений
// компонент векторов и матриц.
//
// Принимает:
// - text : строку, в которой цифры нужно перевести в подстрочный вид.
//
// Возвращает:
// - QString : строку с замененными индексными цифрами.
// ----------------------------------------------------------------------------
QString ToSubscriptDigits(const QString& text)
{
    // Таблица соответствия "обычная цифра -> подстрочная цифра".
    // Порядок элементов совпадает с digitValue() для символов '0'..'9'.
    static const QStringList subscriptDigits = {
        QStringLiteral("₀"),
        QStringLiteral("₁"),
        QStringLiteral("₂"),
        QStringLiteral("₃"),
        QStringLiteral("₄"),
        QStringLiteral("₅"),
        QStringLiteral("₆"),
        QStringLiteral("₇"),
        QStringLiteral("₈"),
        QStringLiteral("₉"),
    };

    // Здесь будет собираться итоговая строка.
    QString result;

    // reserve(...) заранее выделяет память под строку,
    // чтобы уменьшить число перераспределений при конкатенации.
    result.reserve(text.size());

    // Проходим по всем символам исходного текста.
    for (const QChar character : text)
    {
        // Если символ - цифра, заменяем его на подстрочный Unicode-аналог.
        if (character.isDigit())
        {
            result += subscriptDigits[character.digitValue()];
        }
        else
        {
            // Если символ не цифра, оставляем его без изменений.
            result += character;
        }
    }

    // Возвращаем строку с подстрочными индексами.
    return result;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ IndexedSymbol
// ----------------------------------------------------------------------------
// Назначение:
// Построить символ с индексом в подстрочном стиле.
//
// Пример:
// prefix = "x", indices = "12"
// -> "x₁₂"
//
// Принимает:
// - prefix  : базовое буквенное обозначение символа;
// - indices : индексная часть в обычной текстовой форме.
//
// Возвращает:
// - QString : строку вида "символ + подстрочный индекс".
// ----------------------------------------------------------------------------
QString IndexedSymbol(const QString& prefix, const QString& indices)
{
    // Основной символ просто конкатенируется с уже преобразованными индексами.
    return prefix + ToSubscriptDigits(indices);
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BuildComponentHeaders
// ----------------------------------------------------------------------------
// Назначение:
// Построить список заголовков компонент фиксированной размерности.
//
// Пример:
// prefix = "x", dimension = 4
// -> ["x₁", "x₂", "x₃", "x₄"]
//
// Принимает:
// - prefix    : буквенный префикс компоненты;
// - dimension : количество компонент, для которых нужно построить подписи.
//
// Возвращает:
// - QStringList : список готовых заголовков компонент.
// ----------------------------------------------------------------------------
QStringList BuildComponentHeaders(const QString& prefix, std::size_t dimension)
{
    // Итоговый список заголовков.
    QStringList headers;

    // Формируем заголовок для каждой компоненты от 1 до dimension.
    for (std::size_t index = 0; index < dimension; ++index)
    {
        headers << IndexedSymbol(prefix, QString::number(index + 1));
    }

    // Возвращаем готовый список.
    return headers;
}
