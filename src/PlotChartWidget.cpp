// ----------------------------------------------------------------------------
// МЕТОД ResetView - ВОЗВРАТ К ИСХОДНОМУ ОКНУ ПРОСМОТРА
// ----------------------------------------------------------------------------
// Возвращает график к диапазону, который был сохранен после последнего SetSeries().
//
// Принимает:
// - ничего.
//
// Возвращает:
// - ничего.
// ----------------------------------------------------------------------------
// Возвращает график к исходному масштабу и диапазону.
void PlotChartWidget::ResetView()
{
    // ResetView() - это "домой" для графика:
    // он не очищает данные, а только возвращает окно просмотра
    // к тому виду, который был сразу после SetSeries().

    // Если данных нет, логичнее просто перейти к пустому состоянию.
    if (!hasData_)
    {
        Clear();
        return;
    }

    // Сбрасываем шаги делений, чтобы они заново вычислились
    // под исходный диапазон.
    // Это важно, потому что после pan/zoom сетка могла быть
    // пересчитана под другой масштаб.
    xTickStep_ = -1.0;
    yTickStep_ = -1.0;
    // Восстанавливаем сохраненный "домашний" диапазон графика.
    ApplyAxisRange(defaultXMin_, defaultXMax_, defaultYMin_, defaultYMax_);
}


// ----------------------------------------------------------------------------
// МЕТОД ApplyAxisRange - ПРИМЕНЕНИЕ НОВОГО ДИАПАЗОНА ОСЕЙ
// ----------------------------------------------------------------------------
// Централизованно обновляет диапазон, шаг делений, формат подписей,
// служебные серии осей и отображаемые точки графика.
//
// Принимает:
// - xMin, xMax: новый диапазон по оси x;
// - yMin, yMax: новый диапазон по оси y.
//
// Возвращает:
// - ничего.
// ----------------------------------------------------------------------------
void PlotChartWidget::ApplyAxisRange(double xMin, double xMax, double yMin, double yMax)
{
    // Если диапазон по x вырожденный или некорректный,
    // возвращаемся к безопасному запасному интервалу.
    if (!(xMax > xMin) || !std::isfinite(xMin) || !std::isfinite(xMax))
    {
        xMin = 0.0;
        xMax = 1.0;
    }

    if (autoFitYToVisibleX_ && !seriesStates_.isEmpty())
    {
        bool hasVisibleY = false;
        double visibleYMin = 0.0;
        double visibleYMax = 0.0;

        auto accumulateY = [&](double value) {
            if (!std::isfinite(value))
            {
                return;
            }

            if (!hasVisibleY)
            {
                visibleYMin = value;
                visibleYMax = value;
                hasVisibleY = true;
                return;
            }

            visibleYMin = std::min(visibleYMin, value);
            visibleYMax = std::max(visibleYMax, value);
        };

        for (const SeriesState& state : seriesStates_)
        {
            const std::optional<double> leftValue =
                InterpolateSeriesValueAtX(state.fullPoints, state.monotonicByX, state.hoverMode, xMin);
            if (leftValue.has_value())
            {
                accumulateY(*leftValue);
            }

            const std::optional<double> rightValue =
                InterpolateSeriesValueAtX(state.fullPoints, state.monotonicByX, state.hoverMode, xMax);
            if (rightValue.has_value())
            {
                accumulateY(*rightValue);
            }

            for (const QPointF& point : state.fullPoints)
            {
                if (point.x() < xMin)
                {
                    continue;
                }

                if (point.x() > xMax && state.monotonicByX)
                {
                    break;
                }

                if (point.x() <= xMax)
                {
                    accumulateY(point.y());
                }
            }
        }

        if (hasVisibleY)
        {
            const double visibleYSpan = visibleYMax - visibleYMin;
            const double yPad = (visibleYSpan > 0.0 && std::isfinite(visibleYSpan))
                                    ? std::max(1e-9, visibleYSpan * 0.04)
                                    : std::max(1.0, std::fabs(visibleYMin) * 0.05);
            yMin = visibleYMin - yPad;
            yMax = visibleYMax + yPad;
        }
    }

    // Аналогичная защита для диапазона по y.
    if (!(yMax > yMin) || !std::isfinite(yMin) || !std::isfinite(yMax))
    {
        yMin = 0.0;
        yMax = 1.0;
    }

    // Реальные размеры текущего окна по x и y.
    const double xSpan = std::fabs(xMax - xMin);
    const double ySpan = std::fabs(yMax - yMin);

    // Подбираем "красивые" шаги делений для обеих осей.
    double newXTickStep = StableTickStep(xTickStep_, xSpan, 9);
    double newYTickStep = StableTickStep(yTickStep_, ySpan, 9);

    // Если по какой-то причине шаг не получился валидным,
    // заменяем его единицей.
    if (!(newXTickStep > 0.0) || !std::isfinite(newXTickStep))
    {
        newXTickStep = 1.0;
    }
    if (!(newYTickStep > 0.0) || !std::isfinite(newYTickStep))
    {
        newYTickStep = 1.0;
    }

    // Определяем, сколько знаков после запятой понадобится
    // для подписей делений оси.
    const int xDecimals = DecimalsForStep(newXTickStep);
    const int yDecimals = DecimalsForStep(newYTickStep);

    // Включаем динамический режим делений:
    // Qt будет строить сетку по anchor + interval.
    axisX_->setTickType(QValueAxis::TicksDynamic);
    axisY_->setTickType(QValueAxis::TicksDynamic);
    // TickAnchor задает "опорную" точку сетки внутри текущего диапазона.
    axisX_->setTickAnchor(TickAnchorForRange(xMin, newXTickStep));
    axisY_->setTickAnchor(TickAnchorForRange(yMin, newYTickStep));
    // TickInterval задает расстояние между соседними основными делениями.
    axisX_->setTickInterval(newXTickStep);
    axisY_->setTickInterval(newYTickStep);
    // Небольшое число промежуточных линий делает сетку более читаемой.
    axisX_->setMinorTickCount(3);
    axisY_->setMinorTickCount(3);
    // Подписи осей оставляем горизонтальными.
    axisX_->setLabelsAngle(0);
    axisY_->setLabelsAngle(0);
    // Настраиваем строковый формат чисел на осях.
    axisX_->setLabelFormat(QString::asprintf("%%.%df", xDecimals));
    axisY_->setLabelFormat(QString::asprintf("%%.%df", yDecimals));
    // Не разрешаем Qt обрезать подписи многоточием.
    axisX_->setTruncateLabels(false);
    axisY_->setTruncateLabels(false);

    // Сохраняем текущие шаги делений, чтобы использовать их при следующих обновлениях.
    xTickStep_ = newXTickStep;
    yTickStep_ = newYTickStep;

    // Применяем диапазон к осям.
    axisX_->setRange(xMin, xMax);
    axisY_->setRange(yMin, yMax);
    // После этого обновляем служебные оси Ox/Oy
    // и пересобираем видимые пользовательские серии.
    UpdateAxisSeries();
    RefreshSeries();
}

// ----------------------------------------------------------------------------
// МЕТОД RefreshAxes - ПЕРЕСТРОЕНИЕ ОСЕЙ ПО ТЕКУЩЕМУ ДИАПАЗОНУ
// ----------------------------------------------------------------------------
// Принимает:
// - ничего.
//
// Возвращает:
// - ничего.
// ----------------------------------------------------------------------------
// Перестраивает оси исходя из их текущих min/max.
void PlotChartWidget::RefreshAxes()
{
    // Просто перечитываем текущие значения осей и прогоняем их
    // через общую логику ApplyAxisRange().
    ApplyAxisRange(axisX_->min(), axisX_->max(), axisY_->min(), axisY_->max());
}

// ----------------------------------------------------------------------------
// МЕТОД RebuildLegend - ПЕРЕСБОРКА КОМПАКТНОЙ ВНЕШНЕЙ ЛЕГЕНДЫ
// ----------------------------------------------------------------------------
// Штатная QChart legend занимает слишком большую нижнюю полосу внутри chart.
// Поэтому для карточки графика используем отдельный QWidget под chartView:
// он занимает ровно высоту маркеров и текста, без скрытых отступов Qt Charts.
// ----------------------------------------------------------------------------
void PlotChartWidget::RebuildLegend()
{
    if (legendWidget_ == nullptr || legendLayout_ == nullptr)
    {
        return;
    }

    while (QLayoutItem* item = legendLayout_->takeAt(0))
    {
        if (QWidget* widget = item->widget())
        {
            widget->deleteLater();
        }
        delete item;
    }

    if (!legendVisible_ || seriesStates_.size() <= 1)
    {
        legendWidget_->hide();
        return;
    }

    for (qsizetype index = 0; index < seriesStates_.size(); ++index)
    {
        const SeriesState& state = seriesStates_[index];
        const bool selected = highlightedSeriesIndex_ == static_cast<int>(index);
        const bool dimmed = highlightedSeriesIndex_ >= 0 && !selected;

        auto* button = new QPushButton(QString("■ %1").arg(state.name), legendWidget_);
        button->setCheckable(true);
        button->setChecked(selected);
        button->setCursor(Qt::PointingHandCursor);
        button->setToolTip(selected ? "Сбросить выделение серии" : "Выделить эту серию");

        QColor textColor = state.color;
        if (dimmed)
        {
            textColor.setAlpha(115);
        }

        button->setStyleSheet(QString(
                                  "QPushButton {"
                                  " background: %1;"
                                  " color: %2;"
                                  " border: 1px solid %3;"
                                  " border-radius: 8px;"
                                  " padding: 2px 8px;"
                                  " font-family: 'Avenir Next';"
                                  " font-size: 13px;"
                                  " font-weight: %4;"
                                  "}"
                                  "QPushButton:hover {"
                                  " background: #edf5ff;"
                                  " border-color: #9fbbe2;"
                                  "}")
                                  .arg(selected ? "#edf5ff" : "transparent",
                                       textColor.name(QColor::HexArgb),
                                       selected ? "#9fbbe2" : "transparent",
                                       selected ? "700" : "600"));

        connect(button, &QPushButton::clicked, this, [this, index]() {
            SetHighlightedSeries(static_cast<int>(index));
        });

        legendLayout_->addWidget(button, 0, Qt::AlignVCenter);
    }

    legendWidget_->show();
}

void PlotChartWidget::SetHighlightedSeries(int seriesIndex)
{
    if (seriesIndex < 0 || seriesIndex >= seriesStates_.size())
    {
        highlightedSeriesIndex_ = -1;
    }
    else if (highlightedSeriesIndex_ == seriesIndex)
    {
        highlightedSeriesIndex_ = -1;
    }
    else
    {
        highlightedSeriesIndex_ = seriesIndex;
    }

    ApplySeriesHighlight();
    RebuildLegend();
}

void PlotChartWidget::ApplySeriesHighlight()
{
    for (qsizetype index = 0; index < seriesStates_.size(); ++index)
    {
        SeriesState& state = seriesStates_[index];
        if (state.renderedSeries == nullptr)
        {
            continue;
        }

        QColor color = state.color;
        qreal width = state.width;

        if (highlightedSeriesIndex_ >= 0)
        {
            if (highlightedSeriesIndex_ == index)
            {
                width += 1.4;
            }
            else
            {
                color = QColor("#b7c4d2");
                color.setAlpha(135);
                width = std::max<qreal>(0.8, width * 0.55);
            }
        }

        QPen pen(color);
        pen.setWidthF(width);
        pen.setStyle(state.penStyle);
        state.renderedSeries->setPen(pen);
    }
}

// ----------------------------------------------------------------------------
// МЕТОД UpdateAxisSeries - ПЕРЕСТРОЕНИЕ ВНУТРЕННИХ ОСЕЙ OX/OY
// ----------------------------------------------------------------------------
// Принимает:
// - ничего.
//
// Возвращает:
// - ничего.
// ----------------------------------------------------------------------------
// Обновляет внутренние служебные серии, которые отвечают за отрисовку Ox и Oy.
void PlotChartWidget::UpdateAxisSeries()
{
    // Если служебные серии еще не созданы, обновлять нечего.
    if (axisXSeries_ == nullptr || axisYSeries_ == nullptr)
    {
        return;
    }

    // Полностью перестраиваем обе линии осей заново.
    // Это проще и надежнее, чем пытаться частично обновлять их точки,
    // потому что после pan/zoom диапазон мог измениться произвольным образом.
    axisXSeries_->clear();
    axisYSeries_->clear();

    // Считываем текущий видимый диапазон.
    const double xMin = axisX_->min();
    const double xMax = axisX_->max();
    const double yMin = axisY_->min();
    const double yMax = axisY_->max();

    // Горизонтальную ось Ox рисуем только если y = 0
    // попадает в видимый диапазон.
    // Если ноль по y сейчас не виден, то и внутренняя ось Ox
    // внутри plotArea появляться не должна.
    if (yMin <= 0.0 && yMax >= 0.0)
    {
        // Для оси Ox достаточно двух точек:
        // левой и правой границы видимого окна при y = 0.
        axisXSeries_->append(xMin, 0.0);
        axisXSeries_->append(xMax, 0.0);
    }

    // Вертикальную ось Oy рисуем только если x = 0
    // попадает в видимый диапазон.
    // Аналогично ось не должна появляться,
    // если ноль по x ушел за границу текущего окна.
    if (xMin <= 0.0 && xMax >= 0.0)
    {
        // Для оси Oy строим отрезок от нижней до верхней границы окна.
        axisYSeries_->append(0.0, yMin);
        axisYSeries_->append(0.0, yMax);
    }
}

// ----------------------------------------------------------------------------
// МЕТОД RefreshSeries - ОБНОВЛЕНИЕ ОТОБРАЖАЕМЫХ ПОЛЬЗОВАТЕЛЬСКИХ СЕРИЙ
// ----------------------------------------------------------------------------
// Принимает:
// - ничего.
//
// Возвращает:
// - ничего.
// ----------------------------------------------------------------------------
// Перерисовывает все пользовательские серии с учетом текущего окна по x.
void PlotChartWidget::RefreshSeries()
{
    // Текущая видимая область по x нужна,
    // чтобы BuildRenderedPoints мог отфильтровать лишние точки.
    const double visibleXMin = axisX_->min();
    const double visibleXMax = axisX_->max();

    // Обновляем каждую пользовательскую серию отдельно.
    for (SeriesState& state : seriesStates_)
    {
        // Если реальная серия Qt у этого состояния отсутствует,
        // просто пропускаем его.
        if (state.renderedSeries == nullptr)
        {
            continue;
        }

        // Полностью заменяем набор точек отображаемой серии
        // на пересчитанную видимую/прореженную версию.
        // Благодаря этому на экран попадает не весь массив fullPoints,
        // а только тот его фрагмент, который реально нужен в текущем масштабе.
        state.renderedSeries->replace(
            BuildRenderedPoints(state.fullPoints, state.monotonicByX, visibleXMin, visibleXMax));
    }
}

// ----------------------------------------------------------------------------
// МЕТОД PanByPixels - СДВИГ ОКНА ПРОСМОТРА ПО PIXEL DELTA
// ----------------------------------------------------------------------------
// Принимает:
// - pixelDelta: смещение в пикселях viewport.
//
// Возвращает:
// - ничего.
// ----------------------------------------------------------------------------
// Сдвигает видимую область графика на величину, заданную в пикселях viewport.
void PlotChartWidget::PanByPixels(const QPointF& pixelDelta)
{
    // pixelDelta приходит в координатах экрана,
    // но нам нужно сдвигать не пиксели, а диапазоны осей графика.

    // Без данных, chart или корректного delta сдвиг невозможен.
    if (!hasData_ || chart_ == nullptr || !(std::isfinite(pixelDelta.x()) && std::isfinite(pixelDelta.y())))
    {
        return;
    }

    // plotArea определяет реальную рабочую область графика без внешних полей.
    const QRectF plotArea = chart_->plotArea();
    // При слишком маленькой области пересчет становится нестабильным.
    if (!(plotArea.width() > 1.0) || !(plotArea.height() > 1.0))
    {
        return;
    }

    // Текущий видимый диапазон осей.
    const double currentXMin = axisX_->min();
    const double currentXMax = axisX_->max();
    const double currentYMin = axisY_->min();
    const double currentYMax = axisY_->max();
    const double currentXSpan = currentXMax - currentXMin;
    const double currentYSpan = currentYMax - currentYMin;

    // Если диапазон вырожден, pan выполнять нельзя.
    if (!(currentXSpan > 0.0) || !(currentYSpan > 0.0))
    {
        return;
    }

    // Переводим смещение курсора в пикселях viewport
    // в смещение диапазона осей в координатах данных.
    // Идея простая:
    // - если пользователь сдвинулся на долю ширины plotArea,
    //   то и диапазон x нужно сдвинуть на ту же долю currentXSpan;
    // - аналогично для оси y.
    //
    // Знак по x инвертируется, потому что визуальное движение содержимого
    // и изменение числового диапазона происходят в противоположных направлениях.
    const double dx = -(pixelDelta.x() / plotArea.width()) * currentXSpan;
    const double dy = (pixelDelta.y() / plotArea.height()) * currentYSpan;
    // Применяем сдвиг к обеим осям.
    ApplyAxisRange(currentXMin + dx, currentXMax + dx, currentYMin + dy, currentYMax + dy);
}

// ----------------------------------------------------------------------------
// МЕТОД ZoomAt - МАСШТАБИРОВАНИЕ ОТНОСИТЕЛЬНО УКАЗАННОЙ ТОЧКИ
// ----------------------------------------------------------------------------
// Принимает:
// - viewportPos: точку viewport, относительно которой нужно выполнить zoom;
// - factor: множитель масштаба.
//
// Возвращает:
// - ничего.
// ----------------------------------------------------------------------------
// Масштабирует график относительно точки под курсором или жестом.
void PlotChartWidget::ZoomAt(const QPointF& viewportPos, double factor)
{
    // Идея ZoomAt() такая:
    // пользователь указывает некоторую точку на экране,
    // и именно вокруг этой точки график должен увеличиваться или уменьшаться.
    // То есть после zoom курсор должен по возможности остаться "над тем же местом данных",
    // а не просто над центром нового окна.

    // Проверяем, что есть данные, корректный множитель zoom
    // и все необходимые объекты для преобразования координат.
    if (!hasData_ || !(factor > 0.0) || !std::isfinite(factor) || chartView_ == nullptr ||
        chart_ == nullptr || anchorSeries_ == nullptr)
    {
        return;
    }

    // Переводим точку viewport в систему координат chart.
    const QPointF scenePos = chartView_->mapToScene(viewportPos.toPoint());
    const QPointF chartPos = chart_->mapFromScene(scenePos);
    const QRectF plotArea = chart_->plotArea();
    // Zoom работает только если курсор действительно находится над plotArea.
    if (!plotArea.contains(chartPos))
    {
        return;
    }

    // anchor - точка данных, относительно которой будет выполняться масштабирование.
    const QPointF anchor = chart_->mapToValue(chartPos, anchorSeries_);
    // Текущий диапазон осей и его размеры.
    const double currentXMin = axisX_->min();
    const double currentXMax = axisX_->max();
    const double currentYMin = axisY_->min();
    const double currentYMax = axisY_->max();
    const double currentXSpan = currentXMax - currentXMin;
    const double currentYSpan = currentYMax - currentYMin;

    // Вырожденный диапазон масштабировать нельзя.
    if (!(currentXSpan > 0.0) || !(currentYSpan > 0.0))
    {
        return;
    }

    // Берем "домашний" диапазон графика как базу,
    // от которой задаем минимально и максимально допустимый zoom.
    // Это защита от двух крайностей:
    // - нельзя приблизить график настолько, чтобы диапазон почти исчез;
    // - нельзя отдалить его до практически бесконечного масштаба.
    const double baseXSpan = std::max(1e-12, std::fabs(defaultXMax_ - defaultXMin_));
    const double baseYSpan = std::max(1e-12, std::fabs(defaultYMax_ - defaultYMin_));
    const double minXSpan = baseXSpan * 1e-6;
    const double minYSpan = baseYSpan * 1e-6;
    const double maxXSpan = baseXSpan * 1000.0;
    const double maxYSpan = baseYSpan * 1000.0;

    // Новый размер окна после zoom,
    // ограниченный разумными пределами.
    // Здесь factor интерпретируется так:
    // - factor < 1 уменьшает диапазон, то есть приближает;
    // - factor > 1 увеличивает диапазон, то есть отдаляет.
    const double newXSpan = std::clamp(currentXSpan * factor, minXSpan, maxXSpan);
    const double newYSpan = std::clamp(currentYSpan * factor, minYSpan, maxYSpan);

    // Если новый диапазон получился некорректным, выходим.
    if (!(newXSpan > 0.0) || !(newYSpan > 0.0))
    {
        return;
    }

    // xRatio/yRatio показывают, где внутри текущего окна находится anchor:
    // 0 означает левую/нижнюю границу, 1 - правую/верхнюю.
    // Например, xRatio = 0.25 означает, что anchor находится
    // на четверти ширины окна от его левого края.
    const double xRatio = (anchor.x() - currentXMin) / currentXSpan;
    const double yRatio = (anchor.y() - currentYMin) / currentYSpan;
    // На всякий случай ограничиваем их диапазоном [0; 1].
    const double clampedXRatio = std::clamp(xRatio, 0.0, 1.0);
    const double clampedYRatio = std::clamp(yRatio, 0.0, 1.0);

    // Подбираем новый диапазон так,
    // чтобы anchor остался под той же точкой курсора после zoom.
    // Для этого:
    // - откладываем от anchor влево новую ширину, умноженную на долю clampedXRatio;
    // - затем восстанавливаем правую границу как newXMin + newXSpan;
    // - аналогично поступаем по оси y.
    const double newXMin = anchor.x() - newXSpan * clampedXRatio;
    const double newXMax = newXMin + newXSpan;
    const double newYMin = anchor.y() - newYSpan * clampedYRatio;
    const double newYMax = newYMin + newYSpan;

    // Применяем новый диапазон.
    ApplyAxisRange(newXMin, newXMax, newYMin, newYMax);
}
