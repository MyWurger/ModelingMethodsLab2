// ----------------------------------------------------------------------------
// ФУНКЦИЯ MakeSingleRowMatrix
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать одномерный вектор в матрицу из одной строки.
//
// Это полезно в тех местах GUI, где все матричные и векторные объекты
// отображаются единым виджетом, ожидающим именно NumericMatrix.
//
// Принимает:
// - vector : исходный числовой вектор.
//
// Возвращает:
// - NumericMatrix : матрицу, состоящую из одной строки с элементами vector.
// ----------------------------------------------------------------------------
NumericMatrix MakeSingleRowMatrix(const NumericVector& vector)
{
    // Конструируем матрицу сразу списком инициализации:
    // единственная строка этой матрицы - переданный вектор.
    return {vector};
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BuildPreviewMatrix
// ----------------------------------------------------------------------------
// Назначение:
// Построить preview-матрицу для совместного показа первых векторов U и X.
//
// На выходе каждая строка содержит:
// 1) номер наблюдения;
// 2) компоненты соответствующего вектора U;
// 3) компоненты соответствующего вектора X.
//
// Принимает:
// - result : полный результат моделирования, содержащий preview-данные.
//
// Возвращает:
// - NumericMatrix : готовую таблицу первых наблюдений для GUI-представления.
// ----------------------------------------------------------------------------
NumericMatrix BuildPreviewMatrix(const SamplingResult& result)
{
    // Итоговая матрица preview-таблицы, которая будет заполнена строка за строкой.
    NumericMatrix matrix;

    // Защищаемся от рассинхронизации двух preview-массивов:
    // используем только то количество строк, которое гарантированно есть в обоих контейнерах.
    const std::size_t rows = std::min(result.independentPreviewSamples.size(), result.transformedPreviewSamples.size());

    // Сразу резервируем место под все строки, чтобы уменьшить число realocation.
    matrix.reserve(rows);

    // Последовательно собираем каждую строку preview-таблицы.
    for (std::size_t row = 0; row < rows; ++row)
    {
        // Временный вектор значений одной строки:
        // [номер наблюдения | компоненты U | компоненты X].
        NumericVector values;

        // Резервируем точный или почти точный размер строки,
        // чтобы вставки не приводили к лишним перераспределениям памяти.
        values.reserve(1 + result.independentPreviewSamples[row].size() + result.transformedPreviewSamples[row].size());

        // Первым столбцом кладем номер наблюдения.
        // Используем +1, потому что для пользователя нумерация начинается с 1, а не с 0.
        values.push_back(static_cast<double>(row + 1));

        // Затем добавляем все компоненты независимого вектора U для текущего наблюдения.
        //
        // Разберем этот insert по частям:
        // 1) values.end() указывает позицию вставки - самый конец уже собранной строки;
        //    то есть новые значения будут не вставлены "в середину",
        //    а именно дописаны после номера наблюдения.
        // 2) result.independentPreviewSamples[row].begin() указывает на первый элемент
        //    текущего preview-вектора U.
        // 3) result.independentPreviewSamples[row].end() указывает на позицию
        //    сразу после последнего элемента этого же вектора.
        //
        // В сумме эта команда переносит в values весь диапазон компонент U
        // для текущего номера наблюдения row.
        values.insert(values.end(),
                      result.independentPreviewSamples[row].begin(),
                      result.independentPreviewSamples[row].end());

        // После этого аналогичным образом дописываем компоненты преобразованного вектора X.
        //
        // Логика здесь полностью та же:
        // 1) values.end() снова означает "писать в конец уже накопленной строки";
        // 2) begin() и end() задают весь диапазон элементов preview-вектора X;
        // 3) в результате после номера наблюдения и компонент U
        //    в строку последовательно добавляются все компоненты X.
        //
        // Именно так формируется итоговая структура строки preview-таблицы:
        // [номер наблюдения | U_1 ... U_n | X_1 ... X_n].
        values.insert(values.end(),
                      result.transformedPreviewSamples[row].begin(),
                      result.transformedPreviewSamples[row].end());

        // Готовую строку перемещаем в итоговую матрицу.
        // Это дешевле, чем копировать уже заполненный временный вектор.
        matrix.push_back(std::move(values));
    }

    // Возвращаем полностью собранную preview-матрицу.
    return matrix;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BuildHistogramOutline
// ----------------------------------------------------------------------------
// Назначение:
// Построить ломаную-контур гистограммы по набору интервалов HistogramBin.
//
// На выходе получается последовательность точек, пригодная для рисования
// ступенчатого контура поверх гистограммы в PlotChartWidget.
//
// Принимает:
// - bins : набор интервалов гистограммы с их плотностями.
//
// Возвращает:
// - QVector<QPointF> : набор точек контура гистограммы.
// ----------------------------------------------------------------------------
QVector<QPointF> BuildHistogramOutline(const std::vector<HistogramBin>& bins)
{
    // Итоговая серия точек, из которой потом будет построена графическая ломаная.
    QVector<QPointF> points;

    // Для каждого интервала потребуется по две горизонтальные вершины,
    // плюс стартовая и финальная точки на оси OX.
    points.reserve(static_cast<int>(bins.size() * 2 + 2));

    // Если интервалов нет, то и строить нечего:
    // возвращаем пустую серию без дальнейших вычислений.
    if (bins.empty())
    {
        return points;
    }

    // Начинаем контур с точки на оси OX у левой границы первого интервала.
    points.append(QPointF(bins.front().left, 0.0));

// Для каждого интервала добавляем две точки с одинаковой высотой density:
// левую и правую границу, чтобы получилась горизонтальная "ступень".
    //
    // Математически это означает следующее:
    // на каждом интервале [left, right] значение гистограммной плотности
    // считается постоянным и равно bin.density.
    // Поэтому вместо наклонной линии нам нужны две точки с одинаковой ординатой,
    // чтобы график рисовался как кусочно-постоянная ступенчатая функция.
    for (const HistogramBin& bin : bins)
    {
        // Левая вершина текущего прямоугольного сегмента.
        points.append(QPointF(bin.left, bin.density));

        // Правая вершина того же сегмента.
        points.append(QPointF(bin.right, bin.density));
    }

    // Замыкаем контур возвращением на ось OX у правой границы последнего интервала.
    points.append(QPointF(bins.back().right, 0.0));

    // Возвращаем готовый набор точек ступенчатой ломаной.
    return points;
}

