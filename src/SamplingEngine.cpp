// ----------------------------------------------------------------------------
// ФУНКЦИЯ IsSquareMatrix
// ----------------------------------------------------------------------------
// Назначение:
// Проверить, является ли матрица квадратной.
//
// Это обязательная предварительная проверка перед разложением Холецкого,
// потому что ковариационная матрица по определению должна быть квадратной.
//
// Принимает:
// - matrix : матрицу, которую нужно проверить.
//
// Возвращает:
// - bool : true, если матрица квадратная, иначе false.
// ----------------------------------------------------------------------------
bool IsSquareMatrix(const NumericMatrix& matrix)
{
    // Пустую матрицу квадратной не считаем:
    // для ковариационной матрицы такой случай не имеет смысла.
    if (matrix.empty())
    {
        return false;
    }

    // У квадратной матрицы число строк совпадает с числом столбцов.
    const std::size_t size = matrix.size();

    // Проверяем, что длина каждой строки равна общему числу строк.
    for (const NumericVector& row : matrix)
    {
        if (row.size() != size)
        {
            return false;
        }
    }

    return true;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ValidateOptions
// ----------------------------------------------------------------------------
// Назначение:
// Проверить корректность параметров моделирования до начала вычислений.
//
// Принимает:
// - options : пользовательские параметры запуска моделирования.
//
// Возвращает:
// - void : при корректных параметрах функция просто завершается,
//          при ошибке генерирует исключение.
// ----------------------------------------------------------------------------
void ValidateOptions(const SamplingOptions& options)
{
    // Размер выборки N должен быть положительным,
    // иначе сами выборочные оценки не имеют смысла.
    if (options.sampleSize == 0)
    {
        throw std::invalid_argument("Размер выборки должен быть больше нуля");
    }

    // Число интервалов гистограммы тоже должно быть положительным,
    // потому что делить диапазон значений на 0 интервалов невозможно.
    if (options.histogramBins == 0)
    {
        throw std::invalid_argument("Число интервалов гистограммы должно быть больше нуля");
    }

    // Preview-count нужен для первых отображаемых наблюдений.
    // Ноль здесь приводит к бессмысленному пустому представлению.
    if (options.previewCount == 0)
    {
        throw std::invalid_argument("Число отображаемых точек должно быть больше нуля");
    }
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ComputeCholeskyFactor
// ----------------------------------------------------------------------------
// Назначение:
// Построить нижнетреугольный фактор A такой, что:
//
// K_X = A * A^T.
//
// Именно этот шаг реализует теоретический алгоритм лабораторной работы:
// по заданной ковариационной матрице K_X получить матрицу A,
// которая затем используется в преобразовании X = A * U + m_X.
//
// Принимает:
// - covariance : теоретическую ковариационную матрицу K_X.
//
// Возвращает:
// - NumericMatrix : нижнетреугольную матрицу A из разложения Холецкого.
// ----------------------------------------------------------------------------
NumericMatrix ComputeCholeskyFactor(const NumericMatrix& covariance)
{
    // Перед началом разложения убеждаемся, что матрица вообще имеет корректную форму.
    if (!IsSquareMatrix(covariance))
    {
        throw std::invalid_argument("Ковариационная матрица должна быть квадратной");
    }

    // Размерность n определяется числом строк квадратной ковариационной матрицы.
    const std::size_t dimension = covariance.size();

    // Инициализируем фактор A как нижнетреугольную матрицу той же размерности,
    // изначально заполненную нулями.
    NumericMatrix factor(dimension, NumericVector(dimension, 0.0));

    // Проходим строка за строкой, последовательно вычисляя элементы матрицы A.
    for (std::size_t i = 0; i < dimension; ++i)
    {
        // Сначала убеждаемся, что covariance симметрична:
        // K_ij должно совпадать с K_ji.
        // Для ковариационной матрицы это обязательное свойство.
        for (std::size_t j = 0; j < dimension; ++j)
        {
            if (std::abs(covariance[i][j] - covariance[j][i]) > 1.0e-12)
            {
                throw std::invalid_argument("Ковариационная матрица должна быть симметричной");
            }
        }

        // Вычисляем диагональный элемент A_ii.
        //
        // Формула Холецкого для диагонали:
        // A_ii = sqrt(K_ii - sum_{p=0}^{i-1} A_ip^2).
        //
        // В diagonalRemainder как раз аккумулируется выражение под корнем.
        double diagonalRemainder = covariance[i][i];
        for (std::size_t p = 0; p < i; ++p)
        {
            // Вычитаем вклад уже найденных элементов текущей строки.
            diagonalRemainder -= factor[i][p] * factor[i][p];
        }

        // Если подкоренное выражение не положительно, матрица не является
        // положительно определенной, а разложение Холецкого в реальном виде невозможно.
        if (!(diagonalRemainder > 0.0))
        {
            throw std::invalid_argument("Матрица варианта не является положительно определённой");
        }

        // Берем квадратный корень и получаем диагональный элемент фактора A.
        factor[i][i] = std::sqrt(diagonalRemainder);

        // Теперь вычисляем элементы ниже диагонали в текущем столбце i:
        //
        // A_ji = (K_ji - sum_{p=0}^{i-1} A_ip * A_jp) / A_ii,  при j > i.
        for (std::size_t j = i + 1; j < dimension; ++j)
        {
            // Начинаем с соответствующего элемента ковариационной матрицы.
            double crossRemainder = covariance[j][i];
            for (std::size_t p = 0; p < i; ++p)
            {
                // Вычитаем вклад уже вычисленных столбцов.
                crossRemainder -= factor[i][p] * factor[j][p];
            }

            // Делим на диагональный элемент A_ii и получаем A_ji.
            factor[j][i] = crossRemainder / factor[i][i];
        }
    }

    // В результате factor содержит именно нижнетреугольную матрицу A,
    // которая удовлетворяет связи K_X = A * A^T.
    return factor;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ MultiplyLowerTriangular
// ----------------------------------------------------------------------------
// Назначение:
// Умножить нижнетреугольную матрицу A на вектор U.
//
// В контексте лабораторной это первая часть формулы:
//
// X = A * U + m_X.
//
// Принимает:
// - factor : нижнетреугольную матрицу A;
// - vector : независимый вектор U.
//
// Возвращает:
// - NumericVector : результат произведения A * U.
// ----------------------------------------------------------------------------
NumericVector MultiplyLowerTriangular(const NumericMatrix& factor, const NumericVector& vector)
{
    // Размерность результата совпадает с числом строк матрицы A.
    const std::size_t dimension = factor.size();

    // Инициализируем вектор результата нулями.
    NumericVector result(dimension, 0.0);

    // Для каждой строки считаем скалярное произведение этой строки
    // на соответствующую часть вектора U.
    for (std::size_t row = 0; row < dimension; ++row)
    {
        // Так как матрица нижнетреугольная, элементы правее диагонали равны нулю,
        // поэтому достаточно суммировать только column <= row.
        for (std::size_t column = 0; column <= row; ++column)
        {
            // Формула обычного матрично-векторного умножения:
            // result[row] += A[row][column] * U[column].
            result[row] += factor[row][column] * vector[column];
        }
    }

    // Возвращаем вектор A * U.
    return result;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ AddVectors
// ----------------------------------------------------------------------------
// Назначение:
// Сложить два вектора покомпонентно.
//
// В лабораторной эта операция реализует добавление математического ожидания:
//
// X = A * U + m_X.
//
// Принимает:
// - left  : левый вектор;
// - right : правый вектор.
//
// Возвращает:
// - NumericVector : покомпонентную сумму двух векторов.
// ----------------------------------------------------------------------------
NumericVector AddVectors(const NumericVector& left, const NumericVector& right)
{
    // Складывать можно только векторы одинаковой размерности.
    if (left.size() != right.size())
    {
        throw std::invalid_argument("Размерности векторов не совпадают");
    }

    // Подготавливаем вектор результата.
    NumericVector result(left.size(), 0.0);

    // Выполняем обычное покомпонентное сложение:
    // result[i] = left[i] + right[i].
    for (std::size_t index = 0; index < left.size(); ++index)
    {
        result[index] = left[index] + right[index];
    }

    return result;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ComputeEmpiricalMean
// ----------------------------------------------------------------------------
// Назначение:
// Вычислить выборочную оценку математического ожидания по полной выборке X.
//
// Принимает:
// - samples : матрицу наблюдений, где каждая строка является одним
//   сгенерированным вектором X.
//
// Возвращает:
// - NumericVector : вектор выборочных средних по всем компонентам.
// ----------------------------------------------------------------------------
NumericVector ComputeEmpiricalMean(const NumericMatrix& samples)
{
    // Если выборка пуста, считать среднее не по чему.
    // В этом случае возвращаем пустой вектор результата.
    if (samples.empty())
    {
        // Пустые фигурные скобки означают:
        // "создай и верни пустой NumericVector".
        return {};
    }

    // Берем первую строку выборки и смотрим, сколько в ней компонент.
    // Это и есть размерность случайного вектора X.
    const std::size_t dimension = samples.front().size();

    // Создаем вектор mean длины dimension.
    // Все его элементы сразу заполняем нулями,
    // потому что дальше будем накапливать в нем суммы по компонентам.
    NumericVector mean(dimension, 0.0);

    // Проходим по всем наблюдениям выборки.
    // Каждая строка sample - это один сгенерированный вектор X^(k).
    for (const NumericVector& sample : samples)
    {
        // Защищаемся от некорректной выборки:
        // если вдруг одна из строк имеет другую длину,
        // то покомпонентно складывать такие векторы нельзя.
        if (sample.size() != dimension)
        {
            throw std::invalid_argument("Все сгенерированные векторы должны иметь одинаковую размерность");
        }

        // Теперь идем по всем компонентам текущего наблюдения:
        // X_1, X_2, ..., X_dimension.
        for (std::size_t index = 0; index < dimension; ++index)
        {
            // Прибавляем текущую компоненту sample[index]
            // к накопленной сумме этой же компоненты mean[index].
            //
            // После обработки всех наблюдений в mean[index]
            // будет лежать сумма всех значений i-й компоненты:
            // sum_{k=1}^{N} x_i^(k).
            mean[index] += sample[index];
        }
    }

    // Размер выборки N нужен как делитель в формуле среднего.
    // Переводим его в double, чтобы деление было вещественным, а не целочисленным.
    const double count = static_cast<double>(samples.size());

    // Теперь в векторе mean пока лежат не средние, а суммы.
    // Чтобы получить средние значения, делим каждую накопленную сумму на N.
    for (double& value : mean)
    {
        // value - это очередная накопленная сумма по одной компоненте.
        // После деления на count она превращается
        // в выборочную оценку математического ожидания этой компоненты:
        // m^_i = (1 / N) * sum_{k=1}^{N} x_i^(k).
        value /= count;
    }

    // Возвращаем готовый вектор выборочных средних.
    return mean;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ComputeEmpiricalCovariance
// ----------------------------------------------------------------------------
// Назначение:
// Вычислить выборочную ковариационную матрицу K^ по сгенерированной выборке X.
//
// Принимает:
// - samples : матрицу наблюдений, где каждая строка является одним
//   сгенерированным вектором X;
// - mean    : уже вычисленное выборочное среднее m^.
//
// Возвращает:
// - NumericMatrix : выборочную ковариационную матрицу K^.
// ----------------------------------------------------------------------------
NumericMatrix ComputeEmpiricalCovariance(const NumericMatrix& samples, const NumericVector& mean)
{
    // Если выборка пуста, ковариацию считать не по чему.
    // В этом случае возвращаем пустую матрицу.
    if (samples.empty())
    {
        // Пустые фигурные скобки здесь означают:
        // "создай и верни пустой NumericMatrix".
        return {};
    }

    // Размерность ковариационной матрицы равна числу компонент случайного вектора X.
    // Это число берем из уже вычисленного выборочного среднего mean.
    const std::size_t dimension = mean.size();

    // Создаем квадратную матрицу dimension x dimension.
    // Сразу заполняем ее нулями, потому что дальше будем
    // постепенно накапливать в ней суммы произведений отклонений.
    NumericMatrix covariance(dimension, NumericVector(dimension, 0.0));

    // Строим выборочную ковариацию по формуле:
    //
    // K^ = (1 / N) * sum_{k=1}^{N} (x^(k) - m^) * (x^(k) - m^)^T.
    //
    // Здесь:
    // - x^(k)  : k-е наблюдение-вектор;
    // - m^     : выборочное среднее;
    // - T      : транспонирование.
    //
    // Ниже эта формула реализуется покомпонентно:
    // отдельно для каждой пары компонент (row, column).

    // Внешний цикл проходит по всем наблюдениям выборки.
    // Каждая строка sample - это один сгенерированный вектор X^(k).
    for (const NumericVector& sample : samples)
    {
        // Идем по строкам будущей ковариационной матрицы.
        // row указывает, для какой компоненты берем первое отклонение.
        for (std::size_t row = 0; row < dimension; ++row)
        {
            // Считаем отклонение текущей row-компоненты
            // от ее выборочного среднего:
            // x_row^(k) - m^_row.
            const double rowDelta = sample[row] - mean[row];

            // Теперь идем по столбцам ковариационной матрицы.
            // column указывает, с какой второй компонентой
            // мы сейчас строим ковариацию.
            for (std::size_t column = 0; column < dimension; ++column)
            {
                // Для текущей пары компонент (row, column)
                // берем произведение двух отклонений:
                // (x_row^(k) - m^_row) * (x_column^(k) - m^_column).
                //
                // Это вклад k-го наблюдения в элемент ковариационной матрицы
                // с индексами [row][column].
                //
                // Мы прибавляем этот вклад к уже накопленной сумме.
                covariance[row][column] += rowDelta * (sample[column] - mean[column]);
            }
        }
    }

    // Сейчас в матрице covariance лежат не готовые ковариации,
    // а суммы по всем наблюдениям.
    //
    // Чтобы получить именно выборочную ковариацию,
    // нужно разделить каждый элемент на размер выборки N.
    const double count = static_cast<double>(samples.size());

    // Проходим по всем строкам матрицы covariance.
    for (NumericVector& row : covariance)
    {
        // Проходим по всем элементам текущей строки
        // и превращаем накопленную сумму в среднее значение.
        for (double& value : row)
        {
            // После деления получаем готовый элемент
            // выборочной ковариационной матрицы.
            value /= count;
        }
    }

    // Возвращаем готовую выборочную ковариационную матрицу K^.
    return covariance;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ComputeCorrelationMatrix
// ----------------------------------------------------------------------------
// Назначение:
// Построить корреляционную матрицу R по известной ковариационной матрице K.
//
// Принимает:
// - covariance : ковариационную матрицу, которую нужно нормировать.
//
// Возвращает:
// - NumericMatrix : корреляционную матрицу R.
// ----------------------------------------------------------------------------
NumericMatrix ComputeCorrelationMatrix(const NumericMatrix& covariance)
{
    // Если матрица ковариации пуста, корреляционную матрицу строить не из чего.
    // В этом случае возвращаем пустую матрицу.
    if (covariance.empty())
    {
        // Пустые фигурные скобки означают:
        // "создай и верни пустой NumericMatrix".
        return {};
    }

    // Размерность корреляционной матрицы совпадает
    // с размерностью ковариационной матрицы.
    const std::size_t dimension = covariance.size();

    // Создаем квадратную матрицу dimension x dimension
    // и сразу заполняем ее нулями.
    // В эту матрицу будем постепенно записывать коэффициенты корреляции.
    NumericMatrix correlation(dimension, NumericVector(dimension, 0.0));

    // Корреляционная матрица R строится из ковариационной K по формуле:
    //
    // R_ij = K_ij / sqrt(K_ii * K_jj).
    //
    // Здесь:
    // - K_ij  : ковариация между i-й и j-й компонентами;
    // - K_ii  : дисперсия i-й компоненты;
    // - K_jj  : дисперсия j-й компоненты.
    //
    // То есть мы берем ковариацию двух компонент
    // и делим ее на произведение их стандартных отклонений.
    //
    // В результате получаем безразмерную меру связи,
    // значение которой обычно лежит в диапазоне от -1 до 1.

    // Идем по всем строкам будущей корреляционной матрицы.
    for (std::size_t row = 0; row < dimension; ++row)
    {
        // Идем по всем столбцам будущей корреляционной матрицы.
        for (std::size_t column = 0; column < dimension; ++column)
        {
            // Вычисляем знаменатель формулы корреляции:
            // sqrt(K_row,row * K_column,column).
            //
            // На диагонали covariance лежат дисперсии компонент.
            // std::max(0.0, ...) здесь нужен как защитный барьер
            // от возможных очень малых отрицательных значений,
            // которые могут появиться из-за численных ошибок.
            const double denominator =
                std::sqrt(std::max(0.0, covariance[row][row]) * std::max(0.0, covariance[column][column]));

            // Если знаменатель положителен, формула корреляции корректна,
            // и мы можем выполнить нормировку ковариации.
            if (denominator > 0.0)
            {
                // Делим ковариацию между компонентами row и column
                // на произведение их стандартных отклонений.
                //
                // Так получаем коэффициент корреляции R_row,column.
                correlation[row][column] = covariance[row][column] / denominator;
            }

            // Для диагональных элементов формально получится:
            // K_ii / sqrt(K_ii * K_ii) = 1.
            //
            // Мы явно записываем 1.0 на диагональ,
            // чтобы это свойство всегда соблюдалось даже при численных погрешностях.
            if (row == column)
            {
                // На диагонали корреляционной матрицы всегда стоят единицы:
                // каждая величина полностью коррелирована сама с собой.
                correlation[row][column] = 1.0;
            }
        }
    }

    // Возвращаем готовую корреляционную матрицу R.
    return correlation;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ ExtractComponent
// ----------------------------------------------------------------------------
// Назначение:
// Извлечь одну фиксированную компоненту X_i из всей выборки,
// то есть получить отдельный столбец матрицы наблюдений.
//
// Принимает:
// - samples         : полную матрицу наблюдений X;
// - componentIndex  : индекс требуемой компоненты.
//
// Возвращает:
// - NumericVector : все значения выбранной компоненты по всей выборке.
// ----------------------------------------------------------------------------
NumericVector ExtractComponent(const NumericMatrix& samples, std::size_t componentIndex)
{
    NumericVector component;
    component.reserve(samples.size());

    for (const NumericVector& sample : samples)
    {
        // Из каждой строки выборки выбираем одну фиксированную компоненту,
        // то есть фактически извлекаем столбец матрицы samples.
        component.push_back(sample[componentIndex]);
    }

    return component;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BuildComponentStatistics
// ----------------------------------------------------------------------------
// Назначение:
// Собрать сводные статистики по одной компоненте выборки X_i,
// чтобы затем показать их в таблицах и карточках интерфейса.
//
// Принимает:
// - values              : все наблюдения выбранной компоненты X_i;
// - componentIndex      : индекс компоненты;
// - theoreticalMean     : теоретическое математическое ожидание этой компоненты;
// - theoreticalVariance : теоретическую дисперсию этой компоненты.
//
// Возвращает:
// - ComponentStatistics : набор эмпирических и теоретических характеристик
//   выбранной компоненты.
// ----------------------------------------------------------------------------
ComponentStatistics BuildComponentStatistics(const NumericVector& values,
                                            std::size_t componentIndex,
                                            double theoreticalMean,
                                            double theoreticalVariance)
{
    // Если столбец значений пуст, статистики по нему определить нельзя.
    // В этом случае сразу сообщаем об ошибке.
    if (values.empty())
    {
        throw std::invalid_argument("Нельзя вычислить статистики по пустому столбцу");
    }

    // Создаем структуру результата,
    // в которую будем постепенно записывать все найденные характеристики компоненты.
    ComponentStatistics statistics;

    // Запоминаем, для какой именно компоненты X_i строятся статистики.
    statistics.componentIndex = componentIndex;

    // Сохраняем теоретическое математическое ожидание этой компоненты,
    // чтобы потом можно было сравнивать теорию и выборку в одном объекте.
    statistics.theoreticalMean = theoreticalMean;

    // Сохраняем теоретическую дисперсию этой же компоненты.
    statistics.theoreticalVariance = theoreticalVariance;

    // Одним вызовом находим итераторы на минимальный и максимальный элементы
    // среди всех наблюдений данной компоненты.
    const auto [minimumIt, maximumIt] = std::minmax_element(values.begin(), values.end());

    // Разыменовываем найденные итераторы и сохраняем сами значения минимума и максимума.
    statistics.minimum = *minimumIt;
    statistics.maximum = *maximumIt;

    // Выборочное среднее считаем по формуле:
    // m^ = (1 / N) * sum x_k.

    // std::accumulate проходит по всему столбцу values
    // и суммирует все его элементы, начиная с 0.0.
    const double sum = std::accumulate(values.begin(), values.end(), 0.0);

    // Делим сумму всех наблюдений на их количество
    // и получаем выборочное среднее данной компоненты.
    statistics.empiricalMean = sum / static_cast<double>(values.size());

    // Далее считаем выборочную дисперсию как среднее квадрата отклонений:
    // D^ = (1 / N) * sum (x_k - m^)^2.

    // Пока здесь будет накапливаться сумма квадратов отклонений от среднего.
    double varianceAccumulator = 0.0;

    // Проходим по всем наблюдениям выбранной компоненты.
    for (double value : values)
    {
        // delta - это отклонение текущего значения
        // от уже найденного выборочного среднего.
        const double delta = value - statistics.empiricalMean;

        // В сумму для дисперсии добавляем квадрат отклонения.
        // Квадрат нужен для того, чтобы:
        // - большие отклонения сильнее влияли на результат;
        // - положительные и отрицательные отклонения не уничтожали друг друга.
        varianceAccumulator += delta * delta;
    }

    // Делим накопленную сумму квадратов отклонений на число наблюдений N
    // и получаем выборочную дисперсию.
    statistics.empiricalVariance = varianceAccumulator / static_cast<double>(values.size());

    // Стандартное отклонение - это квадратный корень из дисперсии.
    // std::max(0.0, ...) нужен как защита от очень малого отрицательного числа,
    // которое теоретически может появиться из-за численных погрешностей.
    statistics.empiricalStandardDeviation = std::sqrt(std::max(0.0, statistics.empiricalVariance));

    // Возвращаем готовый набор статистик по данной компоненте.
    return statistics;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BuildHistogram
// ----------------------------------------------------------------------------
// Назначение:
// Построить гистограмму по значениям одной компоненты выборки.
//
// Принимает:
// - values         : набор наблюдений одной компоненты X_i;
// - histogramBins  : число интервалов разбиения.
//
// Возвращает:
// - std::vector<HistogramBin> : готовые интервалы гистограммы
//   с границами, частотами и плотностями.
// ----------------------------------------------------------------------------
std::vector<HistogramBin> BuildHistogram(const NumericVector& values, std::size_t histogramBins)
{
    // Если набор значений пуст, строить гистограмму не по чему.
    // В этом случае возвращаем пустой набор интервалов.
    if (values.empty())
    {
        // Пустые фигурные скобки означают:
        // "создай и верни пустой std::vector<HistogramBin>".
        return {};
    }

    // Находим сразу два итератора:
    // - на минимальный элемент выборки;
    // - на максимальный элемент выборки.
    const auto [minimumIt, maximumIt] = std::minmax_element(values.begin(), values.end());

    // Разыменовываем итераторы и получаем сами численные границы диапазона.
    double minimum = *minimumIt;
    double maximum = *maximumIt;

    // Если все значения совпали, то max == min,
    // а значит ширина диапазона равна нулю.
    //
    // Гистограмму с нулевой шириной строить нельзя,
    // поэтому искусственно немного расширяем диапазон вокруг этой точки.
    if (!(maximum > minimum))
    {
        minimum -= 0.5;
        maximum += 0.5;
    }

    // Ширина одного интервала гистограммы:
    // h = (max - min) / M,
    // где M - число интервалов.

    // static_cast<double>(histogramBins) нужен,
    // чтобы деление было вещественным.
    const double width = (maximum - minimum) / static_cast<double>(histogramBins);

    // Создаем histogramBins готовых корзин гистограммы.
    // У каждой корзины позже будут заполнены границы,
    // счетчик попаданий и плотность.
    std::vector<HistogramBin> bins(histogramBins);

    // Теперь задаем границы каждого интервала.
    for (std::size_t index = 0; index < histogramBins; ++index)
    {
        // Левая граница i-го интервала: min + i * h.
        bins[index].left = minimum + static_cast<double>(index) * width;

        // Правая граница - левая плюс ширина интервала.
        bins[index].right = bins[index].left + width;
    }

    // Разносим все наблюдения по интервалам гистограммы.
    for (double value : values)
    {
        // По умолчанию отправляем значение в последний интервал.
        // Это нужно для корректного включения правой границы max.
        std::size_t index = histogramBins - 1;

        // Для всех значений, кроме самого максимума,
        // индекс интервала можно вычислить по общей формуле.
        if (value < maximum)
        {
            // Нормируем значение относительно общего диапазона
            // и получаем число "ширин интервала" от minimum до value.
            const double normalized = (value - minimum) / width;

            // Преобразуем нормированное значение в индекс корзины.
            // std::clamp дополнительно гарантирует,
            // что индекс не выйдет за допустимые границы.
            index = std::clamp<std::size_t>(static_cast<std::size_t>(normalized), 0, histogramBins - 1);
        }

        // Увеличиваем счетчик попаданий в найденную корзину.
        bins[index].count += 1;
    }

    // Плотность по интервалу считаем не как "голую" частоту count / N,
    // а как гистограммную плотность:
    //
    // density = count / (N * h),
    //
    // чтобы площадь всех столбиков приближенно давала 1
    // и гистограмму можно было корректно сравнивать с теоретической плотностью.
    const double sampleCount = static_cast<double>(values.size());

    // Проходим по всем интервалам и для каждого считаем плотность.
    for (HistogramBin& bin : bins)
    {
        // count      - сколько наблюдений попало в данный интервал;
        // sampleCount - общее число наблюдений;
        // width      - ширина интервала.
        //
        // Деление на sampleCount * width переводит обычную частоту
        // в плотность, удобную для сравнения с теоретической кривой.
        bin.density = bin.count / (sampleCount * width);
    }

    // Возвращаем полностью заполненный набор интервалов гистограммы.
    return bins;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BuildSummary
// ----------------------------------------------------------------------------
// Назначение:
// Собрать итоговые показатели качества моделирования:
// ошибку среднего, ошибку ковариации и норму Фробениуса.
//
// Принимает:
// - theoreticalMean        : теоретический вектор m_X;
// - theoreticalCovariance  : теоретическую ковариационную матрицу K_X;
// - empiricalMean          : выборочное среднее m^;
// - empiricalCovariance    : выборочную ковариационную матрицу K^;
// - sampleSize             : размер сформированной выборки.
//
// Возвращает:
// - SamplingSummary : итоговый summary-блок для интерфейса.
// ----------------------------------------------------------------------------
SamplingSummary BuildSummary(const NumericVector& theoreticalMean,
                             const NumericMatrix& theoreticalCovariance,
                             const NumericVector& empiricalMean,
                             const NumericMatrix& empiricalCovariance,
                             std::size_t sampleSize)
{
    // Создаем итоговую структуру summary,
    // в которую будем складывать сводные показатели качества моделирования.
    SamplingSummary summary;

    // Размерность случайного вектора X равна длине теоретического вектора среднего.
    summary.dimension = theoreticalMean.size();

    // Сохраняем размер выборки,
    // чтобы он тоже был доступен в итоговом summary-блоке.
    summary.sampleSize = sampleSize;

    // Ошибку среднего берем в равномерной норме:
    // max_i |m^_i - m_i|.

    // Проходим по всем компонентам вектора среднего.
    for (std::size_t index = 0; index < theoreticalMean.size(); ++index)
    {
        // Для каждой компоненты считаем модуль отклонения
        // между эмпирическим средним и теоретическим средним.
        //
        // Затем сохраняем наибольшее из таких отклонений.
        summary.maxMeanError =
            std::max(summary.maxMeanError, std::abs(empiricalMean[index] - theoreticalMean[index]));
    }

    // Для матрицы ковариации считаем сразу две метрики:
    // 1) максимальную покомпонентную ошибку;
    // 2) норму Фробениуса.

    // Здесь будет накапливаться сумма квадратов всех отклонений матрицы K^ от K.
    double frobeniusAccumulator = 0.0;

    // Проходим по всем строкам теоретической ковариационной матрицы.
    for (std::size_t row = 0; row < theoreticalCovariance.size(); ++row)
    {
        // Проходим по всем столбцам текущей строки.
        for (std::size_t column = 0; column < theoreticalCovariance[row].size(); ++column)
        {
            // delta - это отклонение одного конкретного элемента
            // эмпирической ковариационной матрицы от теоретического.
            const double delta =
                empiricalCovariance[row][column] - theoreticalCovariance[row][column];

            // Максимальная ошибка - это максимум абсолютных отклонений по всем элементам.
            // То есть здесь мы ищем самый "плохой" элемент матрицы
            // по величине ошибки.
            summary.maxCovarianceError = std::max(summary.maxCovarianceError, std::abs(delta));

            // Для нормы Фробениуса накапливаем сумму квадратов отклонений:
            // ||K^ - K||_F = sqrt(sum_{i,j} delta_{ij}^2).
            //
            // Пока только суммируем квадраты, без корня.
            frobeniusAccumulator += delta * delta;
        }
    }

    // После того как сумма квадратов всех отклонений собрана,
    // берем квадратный корень и получаем норму Фробениуса.
    summary.frobeniusCovarianceError = std::sqrt(frobeniusAccumulator);

    // Возвращаем готовый summary-блок.
    return summary;
}
